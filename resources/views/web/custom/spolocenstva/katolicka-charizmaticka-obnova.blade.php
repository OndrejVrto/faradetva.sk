    <x-web.page.subsection>
        <x-web.page.text-segment animation="fromleft">
            <p>
                Katolícka charizmatická obnova na Slovensku vidí svoje začiatky v polovici osemdesiatych rokov, najmä prostredníctvom kňaza Václava Kociana a jeho spoločenstva. Začiatkom deväťdesiatych rokov vzniklo Združenie JAS, ktoré malo významnú úlohu na poli Charizmatickej obnovy pri organizovaní charizmatických konferencií. Hnutie sa v súčasnosti snaží o vnútornú transformáciu, aby boli konštituované dôležité oblasti a organizačné štruktúry, potrebné pre fungovanie tak, ako je to známe z okolitých krajín.
            </p>
            <p>
                Služobný tím Katolíckej charizmatickej obnovy na Slovensku tvoria dvaja zástupcovia za každú diecézu - jeden kňaz a jeden laik. Koordinátorom hnutia je Mons. Peter Brodek, kňaz Nitrianskej diecézy a jeho zástupcom je Imrich Degro, kňaz Košickej arcidiecézy. V rámci jednotlivých diecéz sa ku Katolíckej charizmatickej obnove hlási približne 150 spoločenstiev. Pre vnútornú potrebu členov hnutia vychádza časopis Charizma. Každoročnou významnou aktivitou hnutia je celoslovenská konferencia Charizmatickej obnovy.
            </p>
        </x-web.page.text-segment>
        <x-web.page.text-segment animation="fromright">
            <p>
                Katolícka charizmatická obnova sa usiluje prispieť k obráteniu, spáse a posväcovaniu všetkých ľudí a k ich zjednoteniu v účinnom spoločenstve Božieho ľudu. Kladie dôraz na: poznanie Boha ako milujúceho a odpúšťajúceho Otca, nadviazanie osobného vzťahu s Ježišom, potrebu posilňovať v sebe prítomnosť Ducha Svätého, dôležitosť Svätého písma, sviatostí, bohoslužby, modlitby, modlitby chvál, dôležitosť chariziem alebo darov Ducha Svätého pre službu Cirkvi, potrebu budovať spoločenstvo, poslanie ohlasovať evanjelium a vydávať svedectvo.
            </p>
