            <p>
                Katolícka charizmatická obnova nie je hnutie medzi hnutiami, je „hnutie“ Ducha Svätého v&nbsp;celej Cirkvi. Charizmatická obnova vznikla vo februári 1967 v&nbsp;Pittsburghu v&nbsp;USA. Na duchovné cvičenia v&nbsp;Univerzite Duquesne prišlo asi 30 študentov a&nbsp;spolu sa spontánne modlili k&nbsp;Duchu Svätému. Mali ohromujúcu skúsenosť „vyliatia Ducha“.
            </p>

            <p>
                V&nbsp;roku 1975 sa pápež sv. Pavol VI. stretol s&nbsp;vodcami hnutia, a&nbsp;to zanechalo nezmazateľnú stopu na Obnove a&nbsp;mocne ju posunulo v&nbsp;jej duchovnom raste. Spirituálna blízkosť Charizmatickej obnovy pokračovala aj so sv. Jánom Pavlom II., Benediktom XVI. a&nbsp;teraz aj s&nbsp;pápežom Františkom.
            </p>

            <p>
                Katolícka charizmatická obnova na Slovensku vidí svoje začiatky v&nbsp;polovici osemdesiatych rokov, najmä prostredníctvom kňaza Václava Kociana a&nbsp;jeho spoločenstva. Začiatkom deväťdesiatych rokov vzniklo Združenie JAS, ktoré malo významnú úlohu na poli Charizmatickej obnovy pri organizovaní charizmatických konferencií.
            </p>

            <p>
                Hnutie sa v&nbsp;súčasnosti snaží o&nbsp;vnútornú transformáciu, aby boli konštituované dôležité oblasti a&nbsp;organizačné štruktúry, potrebné pre fungovanie tak, ako je to známe z&nbsp;okolitých krajín.
            </p>

            <p>
                Služobný tím Katolíckej charizmatickej obnovy na Slovensku tvoria dvaja zástupcovia za každú diecézu - jeden kňaz a&nbsp;jeden laik. Koordinátorom hnutia je Mons. Peter Brodek, kňaz Nitrianskej diecézy a&nbsp;jeho zástupcom je Imrich Degro, kňaz Košickej arcidiecézy. V&nbsp;rámci jednotlivých diecéz sa ku Katolíckej charizmatickej obnove hlási približne 150 spoločenstiev.
            </p>

            <p>
                Pre vnútornú potrebu členov hnutia vychádza časopis <em>Charizma</em>. Každoročnou významnou aktivitou hnutia je celoslovenská konferencia Charizmatickej obnovy.
            </p>

            <x-partials.picture titleSlug="kcho-009" side="left" dimensionSource="full" columns="4" />

            <p>
                Veriaci z&nbsp;Farnosti Detva sa vo väčšej miere začali zapájať do aktivít Katolíckej charizmatickej obnovy po roku 1996 na podnet vtedajšieho kaplána Dušana Mesíka. V&nbsp;súčasnosti je vo farnosti aktívne jedno menšie spoločenstvo, ktoré sa pravidelne schádza jedenkrát do týždňa.
            </p>

            <p>
                Katolícka charizmatická obnova sa usiluje prispieť k&nbsp;obráteniu, spáse a&nbsp;posväcovaniu všetkých ľudí a&nbsp;k&nbsp;ich zjednoteniu v&nbsp;účinnom spoločenstve Božieho ľudu. Kladie dôraz na poznanie Boha ako milujúceho a&nbsp;odpúšťajúceho Otca, nadviazanie osobného vzťahu s&nbsp;Ježišom, potrebu posilňovať v&nbsp;sebe prítomnosť Ducha Svätého.
            </p>

            <p>
                Ďalej na dôležitosť Svätého písma, sviatostí, bohoslužby, modlitby, modlitby chvál, dôležitosť chariziem alebo darov Ducha Svätého pre službu Cirkvi, potrebu budovať spoločenstvo, poslanie ohlasovať evanjelium a&nbsp;vydávať svedectvo.
            </p>
