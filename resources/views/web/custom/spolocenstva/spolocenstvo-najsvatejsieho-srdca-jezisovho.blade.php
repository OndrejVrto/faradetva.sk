            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="bsr-006" side="left" dimensionSource="medium" columns="2"/>

                <p>
                    Spoločenstvo Najsvätejšieho Srdca Ježišovho v&nbsp;Detve bolo založené farárom Štefanom Pitrofom 16. marca 1890. Do roku 1929 bolo do tohto spoločenstva zapísaných takmer 2&nbsp;000 členov. Spoločenstvo v&nbsp;Detve bolo obnovené 30. januára 2001 a&nbsp;jeho koordinátorom je spoločenstvo vo Farnosti Litava.
                </p>

                <p>
                    V&nbsp;roku 2016 diecézny biskup Marián Chovanec vyhlásil Farnosť Litavu s&nbsp;jej farským Kostolom sv. Margity Antiochijskej za pútnické miesto a&nbsp;stredisko úcty k&nbsp;Najsvätejšiemu Srdcu Ježišovmu v&nbsp;Banskobystrickej diecéze. Spoločenstvo Najsvätejšieho Srdca Ježišovho je duchovné spoločenstvo živých i&nbsp;zosnulých ctiteľov Ježišovho srdca, podľa starobylej cirkevnej tradície a&nbsp;podľa novovekej formy francúzskej svätice Margity Márie Alacoque (1647 - 1690).
                </p>

                <x-partials.picture titleSlug="bsr-009-belko" side="right" dimensionSource="medium" columns="3"/>

                <p>
                    Svätá Margita Mária Alacoque bola členkou Rehole Navštívenia Panny Márie v&nbsp;Paray-le-Monial vo Francúzsku. Dňa 27. decembra 1673 sa jej pri adorácii Oltárnej sviatosti zjavilo v&nbsp;oslňujúcom lesku Božské Srdce, ktoré bolo ovinuté tŕňmi a&nbsp;ozdobené krížom. Podľa jej vlastných slov bola v&nbsp;tej chvíli naplnená nevýslovnou láskou. Druhé zjavenie mala v&nbsp;piatok v&nbsp;oktáve sviatku Božieho tela.
                </p>

                <p>
                    Pán Ježiš jej ukázal svoje Božské Srdce a&nbsp;pritom počula hlas:
                    <cite>
                        „Pozri, toto srdce, ktoré tak veľmi milovalo ľudí, ktoré nič nešetrilo, ba aj samé seba strávilo, len aby ukázalo svoju lásku. Namiesto vďačnosti sa mu však za odplatu od veľmi mnohých dostáva nevďak. Preto od teba žiadam, aby prvý piatok po oktáve slávnosti môjho Božského tela bol ustanovený ako zvláštna slávnosť, aby sa v&nbsp;ten deň uctievalo moje srdce slávnostným odprosovaním a&nbsp;aby v&nbsp;ten deň ľudia pristupovali k&nbsp;svätému prijímaniu s&nbsp;tým úmyslom, aby sa vynahradili nesčíselné zneuctenia, ktorých sa mu dostalo po ten čas, čo bolo vystavené na oltári.“
                    </cite>
                </p>

                <p>
                    Po tomto zjavení Margita ťažko ochorela. Cítila v&nbsp;srdci nesmierny bôľ, ktorý ju už do smrti neopustil. V&nbsp;nedeľu 16. júna 1675 sa jej zjavil Spasiteľ tretíkrát. Znova hovoril o&nbsp;tom, ako ľudia znevažujú jeho lásku a&nbsp;sviatosti. Žiadal Margitu, aby pristupovala prvý piatok každého mesiaca k&nbsp;svätému prijímaniu, aby tak podľa svojich síl naprávala urážky, ktoré postihnú v&nbsp;tom mesiaci Najsvätejšiu sviatosť. Margita odvtedy každú noc zo štvrtka na piatok prežívala smrteľnú úzkosť, ktorú mal Ježiš pred svojím umučením v&nbsp;Getsemanskej záhrade.
                </p>

                <x-partials.picture titleSlug="bsr-004" side="left" dimensionSource="medium" columns="4"/>

                <p>
                    S&nbsp;pomocou svojho duchovného vodcu, jezuitu Klaudia de la Comboniéra, uskutočnila čo jej Pán Ježiš cez zjavenia povedal a&nbsp;kázal rozšíriť ďalej. Pobožnosť k&nbsp;Najsvätejšiemu Srdcu Ježišovmu sa rozšírila do celej Cirkvi ešte počas jej života a&nbsp;dodnes má silnú tradíciu.
                </p>

                <p>
                    Zo zjavení sv. Margity, ktoré opísala vo svojich zápiskoch, sa časom ustálilo dvanásť prisľúbení Božského Srdca, z&nbsp;ktorých najznámejšie je:
                    <cite>
                        „Všetkým, ktorí pristúpia k&nbsp;svätému prijímaniu na prvý piatok počas deviatich mesiacov nepretržite po sebe, sľubujem milosť pokánia pri zomieraní - nezomrú v&nbsp;nemilosti, ani bez sviatostí. Moje srdce im bude bezpečným útočišťom v&nbsp;posledných chvíľach života.“
                    </cite>
                </p>

                <p>
                    Členovia spoločenstva sa usilujú sláviť sviatok Najsvätejšieho Srdca Ježišovho zvláštnou pobožnosťou a&nbsp;prijímaním sviatostí. Na prvý piatok mesiaca sa zúčastňujú svätej omše a&nbsp;prijímajú sväté prijímanie.
                </p>

                <p>
                    Každý člen sa denne pomodlí <em>Otče náš</em>, <em>Zdravas' Mária</em> a&nbsp;<em>Verím v&nbsp;Boha</em> s&nbsp;prosbou:
                    <cite>
                        „Najsvätejšie Srdce Ježišovo daj, aby som ťa vždy viac a&nbsp;viac miloval.“
                    </cite>
                    Celkovým cieľom spoločenstva je oslava Pána Boha a&nbsp;posväcovanie seba i&nbsp;sveta, a&nbsp;to najmä čnostným kresťanským životom podľa vzoru Bohočloveka - Ježiša Krista.
                </p>

