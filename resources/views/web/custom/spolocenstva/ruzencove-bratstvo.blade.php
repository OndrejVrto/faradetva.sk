        <x-web.page.subsection>
            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="rbr-014" side="right" dimensionSource="medium" columns="3"/>

                <p>
                    <strong>Člen <em>Svätého ruženca</em></strong> je povinný pomodliť sa celý posvätný ruženec (tajomstvá radostné, svetla, bolestné, slávnostné) za jeden týždeň na úmysel za obrátenie hriešnikov a&nbsp;za živých i&nbsp;zosnulých členov Ružencového bratstva, za Rehoľu dominikánov, za svoju farnosť a&nbsp;za svojich duchovných pastierov (biskupov a&nbsp;kňazov). Modlitbu posvätného ruženca si môže člen ľubovoľne rozdeliť podľa času, ktorý mu vyhovuje, napr. v&nbsp;jeden deň dva desiatky, v&nbsp;iný tri alebo v&nbsp;jeden deň celý ruženec - potrebné je zachovať postupnosť v&nbsp;modlitbe celého posvätného ruženca (najprv sa pomodliť radostné tajomstvá, potom svetla, bolestné, nakoniec slávnostné). Člen Svätého ruženca nie je viazaný spoločnými stretnutiami členov Živého ruženca, ale je tam vítaný.
                </p>

                <p>
                    <strong>Člen <em>Večného ruženca</em></strong> si zvolí svoj pravidelný „čas bdenia“ raz za mesiac alebo raz za rok, počas ktorého sa pomodlí celý posvätný ruženec s&nbsp;nasledovnými úmyslami: za obrátenie hriešnikov (radostné tajomstvá), za čistotu viery a&nbsp;ohlasovateľov evanjelia (tajomstvá svetla), za večnú spásu umierajúcich (bolestné tajomstvá), za vyslobodenie duší z&nbsp;očistca (slávnostné tajomstvá).
                </p>

                <x-partials.picture titleSlug="rbr-017" side="left" dimensionSource="medium" columns="3"/>

                <p>
                    Pod časom bdenia sa myslí ucelený časový celok - bez prerušenia. Na splnenie povinnosti „času bdenia“ mesačne alebo ročne stačí sa pomodliť súkromne a&nbsp;na ľubovoľnom mieste modlitbu celého posvätného ruženca. Člen Večného ruženca nie je viazaný spoločnými stretnutiami členov Živého ruženca, ale je tam vítaný.
                </p>

                <p>
                    Duchovné sprevádzanie členov ružencových bratstiev na Slovensku je zverené provinčnému promótorovi pre ruženec. Jemu je zverená služba zakladať ružencové bratstvá, koordinovať ružencový apoštolát a&nbsp;navštevovať jednotlivé bratstvá vo farnostiach na území Slovenska. Promótora pre ruženec vymenuje provinciál Rehole dominikánov a&nbsp;vymenovanie podlieha schváleniu generálnej kúrie dominikánov v&nbsp;Ríme. V&nbsp;lete 2017 sa provinčným promótorom pre ruženec na Slovensku stal brat Alan Ján Dely OP.
                </p>

                <x-partials.picture titleSlug="rbr-010" side="right" dimensionSource="medium" columns="1"/>

                <p>
                    V&nbsp;našej farnosti jestvuje dlhodobá tradícia ružencového bratstva. Zásluhou farára Jána Štrbáňa bolo 19.&nbsp;októbra&nbsp;1924 Rehoľou dominikánov oficiálne zriadené Ružencové bratstvo v&nbsp;Detve, do ktorého sa do roku 1946 zapísalo až 915 osôb. V&nbsp;roku 2003 sa uskutočnila reorganizácia miestnych bratstiev, ktorých dovtedajšia činnosť sa 27. apríla 2003 ukončila a&nbsp;začala sa registrácia nových spoločenstiev.
                </p>

                <p>
                    Každá ruža (spoločenstvo - bratstvo Živého ruženca) môže mať 20 členov, ktorí majú bydlisko vo farnosti. V&nbsp;roku 2003 bolo zaregistrovaných 53 ružencových bratstiev (53 ruží) a&nbsp;z&nbsp;tohto počtu zostalo v&nbsp;roku 2022 aktívnych 46 ruží. Jednotlivé ruže sa postupne v&nbsp;priebehu kalendárneho roka striedajú v&nbsp;upratovaní farského kostola, raz do týždňa v&nbsp;sobotu.
                </p>

