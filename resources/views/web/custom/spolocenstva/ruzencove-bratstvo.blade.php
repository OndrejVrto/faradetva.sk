        <x-web.page.subsection>
            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="rbr-016" side="right" dimensionSource="medium" columns="2"/>

                <p>
                    Ružencové bratstvo je združenie duchovne zjednotených veriacich formovaných dominikánskou spiritualitou, ktorí si osobitným spôsobom uctievajú Pannu Máriu a&nbsp;modlitbou posvätného ruženca vyprosujú potrebné dobrá pre seba, Cirkev a&nbsp;svet. Cieľom každého člena ružencového bratstva je zároveň snaha o&nbsp;neustály pokrok v&nbsp;láske k&nbsp;Bohu a&nbsp;ľuďom, o&nbsp;neustále premieňanie modlitby na konkrétne skutky.
                </p>

                <x-partials.picture titleSlug="rbr-007" side="left" dimensionSource="medium" columns="2"/>

                <p>
                    Ružencové bratstvá sú organizované skupiny veriacich, ktoré svojou činnosťou sledujú spoločný cieľ - šírenie dobra cez osobné sebaposväcovanie, cez prehlbovanie viery jednotlivých členov, cez prehlbovanie ich lásky k&nbsp;Bohu a&nbsp;k&nbsp;ľuďom okolo nich.
                </p>

                <p>
                    Členmi ružencového bratstva sa veriaci stávajú na základe slobodného rozhodnutia, keďže tým sa zaväzujú prijať na seba všetky povinnosti a&nbsp;práva vyplývajúce z&nbsp;členstva.
                </p>

                <p>
                    Členom ružencového bratstva môže byť pokrstený katolík, ktorý má minimálne 9 rokov (výnimka je možná), ktorý sa slobodne rozhodne, že na seba vezme práva a&nbsp;povinnosti vyplývajúce z&nbsp;členstva v&nbsp;ružencovom bratstve a&nbsp;pri vstupe do bratstva si vyberie jednu z&nbsp;foriem modlitby posvätného ruženca a&nbsp;stane sa tak členom <em>Živého</em>, <em>Svätého</em> alebo <em>Večného ruženca</em>. Záväzky, ktoré člen na seba prevezme vstupom do ruženca, sú jeho osobnými záväzkami, to znamená, že ich musí plniť sám, nemôže tieto záväzky za neho plniť iná osoba.
                </p>

                <p>
                    <strong>Člen <em>Živého ruženca</em></strong> je povinný modliť sa každý deň jeden desiatok posvätného ruženca na úmysel za obrátenie hriešnikov a&nbsp;za živých i&nbsp;zosnulých členov Ružencového bratstva, za Rehoľu dominikánov, za svoju farnosť a&nbsp;za svojich duchovných pastierov (biskupov a&nbsp;kňazov). Je povinný raz za mesiac sa zísť na spoločnom stretnutí - modlitbe, po ktorom sa uskutoční výmena tajomstiev.
                </p>
            </x-web.page.text-segment>
