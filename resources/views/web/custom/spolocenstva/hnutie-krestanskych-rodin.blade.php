        <x-web.page.text-segment animation="fromleft">
            <p>
                Hnutie kresťanských rodín bolo na Slovensku založené v&nbsp;roku 1969 na báze malých rodinných spoločenstiev, ktorých vznik bol iniciovaný po celom Slovensku. Hnutie pracovalo tri desaťročia v&nbsp;ilegalite pod duchovným vedením vtedajšieho tajného biskupa Jána Chrizostoma Korca a&nbsp;v&nbsp;spolupráci s&nbsp;kňazmi z&nbsp;pastorácie i&nbsp;mnohými tajnými rehoľnými kňazmi. Projektantmi myšlienky malých rodinných spoločenstiev a&nbsp;jej konkrétnymi realizátormi najmä medzi mládežou a&nbsp;študentmi boli dve významné osobnosti náboženského života u&nbsp;nás - RNDr. Vladimír Jukl a&nbsp;MUDr. Silvester Krčméry.
            </p>
            <p>
                Spiritualita Hnutia kresťanských rodín na Slovensku bola schválená 29. októbra 1993. Je charakterizovaná prevenciou a&nbsp;rozvojom harmonického života rodín na princípoch kresťanskej viery, rodinnej lásky, zodpovednej slobody, služby a&nbsp;jednoty s&nbsp;Katolíckou cirkvou. Východiskami spirituality sú: Sväté písmo, učenie Cirkvi, aktuálne skutočnosti dnešného sveta. Hnutie pracuje na Slovensku s&nbsp;13 500 rodinami a&nbsp;má 65 000 členov. Jeho súčasným čestným predsedom je doc. Vladimír Ďurikovič.

            </p>
