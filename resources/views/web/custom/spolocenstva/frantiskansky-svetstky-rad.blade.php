                <p>
                    V roku 1785 bol zhotovený inventár zrušenej Spoločnosti kordigerov v&nbsp;Detve, ktorá pravdepodobne musela zaniknúť na základe patentu o&nbsp;zrušení žobravých a&nbsp;rozjímavých reholí, ktorý vydal cisár Jozef&nbsp;II. v&nbsp;roku 1782.
                </p>

                <p>
                    Tretí rád sv. Františka bol v&nbsp;Detve opäť obnovený až v&nbsp;roku 1924 zásluhou farára Jána Štrbáňa, bývalého františkána. Spoločenstvo viedol sám Štrbáň, ktorý bol jeho členom a&nbsp;zároveň riaditeľom. V&nbsp;rozmedzí rokov 1924 - 1953 sa pridalo k&nbsp;františkánskym terciárom v&nbsp;Detve 391 osôb. Posledným zapísaným členom v&nbsp;roku 1953 bol „apoštol Podpoľania“, pán Štefan Vlk.
                </p>


                <p>
                    Po Nežnej revolúcii a&nbsp;následnej náboženskej slobode začalo päť <em>terciárok</em> navštevovať bratstvo Františkánskeho svetského rádu v&nbsp;Lučenci. Po niekoľkých rokoch bolo 11. marca 2006 duchovným asistentom pátrom Jakubom Martausom OFM založené v&nbsp;Detve dočasné <em>Bratstvo sv. Anežky Českej</em>.
                </p>

                <x-partials.picture titleSlug="ofs-007" side="right" dimensionSource="medium" columns="4" />

                <p>
                    Na sviatok patrónky bratstva 2. marca 2008 bolo toto miestne bratstvo v&nbsp;Detve kánonicky zriadené - oficiálne ustanovené. V&nbsp;tom istom roku sa však zistilo, že nové bratstvo nebolo potrebné zriaďovať, pretože sa našla posledná žijúca členka pôvodného Tretieho rádu sv. Františka, ktorej otec patril medzi zakladateľov bratstva v&nbsp;roku 1924 a&nbsp;ona sama do neho vstúpila ešte v&nbsp;roku 1941. Následne boli akoby tieto dve osobitné bratstvá zlúčené do jedného.
                </p>

                <p>
                    Bratstvo Františkánskeho svetského rádu v&nbsp;Detve sa pravidelne stretáva dvakrát do mesiaca v&nbsp;pastoračnom centre farnosti. Hierarchicky patrí pod Stredoslovenský región Národného bratstva OF S&nbsp;Sedembolestnej Panny Márie. Je súčasťou Medzinárodného bratstva OFS, toho istého Tretieho rádu, ktorý pred 800 rokmi založil patrón kostola i&nbsp;celej farnosti, sv. František z&nbsp;Assisi.
                </p>

