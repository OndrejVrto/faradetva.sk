            <x-partials.picture titleSlug="ocds-011" side="left" dimensionSource="medium" columns="3" />

            <p>
                Svetský rád bosých karmelitánov (Ordo Carmelitanum Discalceaforum Secularis) je spoločenstvo veriacich, ktorí sa zaväzujú usilovať sa o&nbsp;evanjeliovú dokonalosť vo svete podľa evanjeliových rád čistoty, chudoby a&nbsp;poslušnosti podľa stavu a&nbsp;podľa blahoslavenstiev, pričom sa vo svojom kresťanskom živote inšpirujú a&nbsp;povzbudzujú spiritualitou tereziánskeho Karmelu.
            </p>

            <p>
                Jeho členovia patria úplne do karmelitánskej rodiny, sú členmi karmelitánskeho rádu. Spája ich s&nbsp;ním bratské spoločenstvo, spoločné duchovné dobrá, účasť na tom istom povolaní k&nbsp;svätosti a&nbsp;na spoločnom poslaní Cirkvi pri zachovaní rozdielneho stavu života.
            </p>

            <p>
                Členovia svetského rádu sa vyznačujú citlivosťou na prejavy Božej lásky, poslušnosťou voči Cirkvi, láskou k&nbsp;rozjímavej modlitbe. Ich povolaním je život v&nbsp;Božej prítomnosti, pestovanie kontemplatívnej modlitby, modlitby za kňazov a&nbsp;kňazské povolania, za Cirkev na Slovensku, neustále prežívanie dôverného spojenia s&nbsp;Pannou Máriou - Kráľovnou Karmelu.
            </p>

            <x-partials.picture titleSlug="ocds-006" side="right" dimensionSource="medium" columns="3" />

            <p>
                V&nbsp;živote karmelitána Panna Mária slúži predovšetkým ako príklad vernosti v&nbsp;počúvaní Pána a&nbsp;v&nbsp;službe Pánovi. Toto vnútorné zapojenie do intenzívneho mariánskeho života sa má prejaviť aj navonok vo forme každodenných zvláštnych skutkov ku cti Najsvätejšej Panny, ako aj nosením karmelitánskeho škapuliara.
            </p>


            <p>
                Svetskí karmelitáni sú verní výzve a&nbsp;príkladu Pána veľa sa modliť, ako aj hlavnému príkazu prvotnej karmelitánskej reguly - rozjímať o&nbsp;slovách Pána a&nbsp;bdieť na modlitbách. Život modlitby je najdôležitejšou úlohou a&nbsp;súčasne charakteristickou črtou Karmelu.
            </p>

            <p>
                Členovia svetského rádu majú stále pestovať vo vnútornej sústredenosti ducha modlitby, ktorej zasväcujú najmenej pol hodiny denne: majú sa usilovať počúvať Božie slovo, najmä v&nbsp;posvätnej liturgii, a&nbsp;duchovným čítaním rozmnožovať v&nbsp;sebe vznešené poznanie Ježiša Krista, čerpané z&nbsp;prameňov kresťanskej a&nbsp;karmelitánskej spirituality, ako aj z&nbsp;učenia a&nbsp;charizmy svätých Karmelu.
            </p>

            <x-partials.picture titleSlug="ocds-008" side="left" dimensionSource="medium" columns="4" />

            <p>
                Členovia svetského rádu sú povolaní žiť vo vernosti Ježišovi Kristovi pod ochranou Preblahoslavenej Panny Márie z&nbsp;hory Karmel, v&nbsp;biblickej tradícii proroka Eliáša. Inšpirovaní učením svätej Terézie od Ježiša a&nbsp;svätého Jána od Kríža sa usilujú prehlbovať svoj kresťanský záväzok prijatý vo sviatosti krstu.
            </p>

            <p>
                Svetskí karmelitáni sú spolu s&nbsp;rehoľníkmi a&nbsp;mníškami synmi a&nbsp;dcérami Preblahoslavenej Panny Márie z&nbsp;hory Karmel a&nbsp;svätej Terézie Veľkej. Z&nbsp;tohto dôvodu majú účasť na tej istej charizme ako rehoľníci, každý podľa svojho osobitného stavu života.
            </p>

            <p>
                Je to jedna rodina, ktorá sa zúčastňuje na tých istých duchovných dobrách, s&nbsp;tým istým povolaním k&nbsp;svätosti a&nbsp;s&nbsp;tým istým apoštolským poslaním. Svetskí členovia prispievajú do rádu dobrami vlastnými pre ich svetský stav života.
            </p>

            <p>
                Do svetského rádu bosých karmelitánov môže vstúpiť katolík vo veku od 18 do 60 rokov, ktorý sa usiluje o&nbsp;skutočný kresťanský život a&nbsp;evanjeliovú dokonalosť a&nbsp;túži žiť zasvätený život vo svete. Môže to byť osoba žijúca v&nbsp;ktoromkoľvek životnom stave, či už v&nbsp;manželstve, alebo ako vdovec/vdova, slobodný/slobodná.
            </p>
