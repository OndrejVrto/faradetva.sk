            <p>
                Svetský rád bosých karmelitánov začal byť v&nbsp;Detve aktívny od roku 2008, po tom ako bol dobudovaný kláštor bosých karmelitánok, kde bol neskôr vytvorený aj priestor pre stretávanie sa jeho členov. Novo-formujúceho spoločenstva sa ujal karmelitán Andrej Valent, rodák z&nbsp;Detvy, ktorý v&nbsp;tom čase pôsobil v&nbsp;Banskej Bystrici.
            </p>

            <p>
                Začínali ôsmi, ďalší viacerí záujemcovia o&nbsp;karmelitánsku spiritualitu rozlišovali svoje povolanie do svetského rádu. Po usmerneniach a&nbsp;radách delegáta pre OCDS, Lukáša Kasperka z&nbsp;Krakova, začali záujemcovia po uplynutí stanoveného času vstupovať do noviciátu a&nbsp;prijímať dočasné i&nbsp;doživotné prísľuby. V&nbsp;rokoch 2009 - 2020 bol asistentom svetského rádu v&nbsp;Detve karmelitán Miloš Viktorín, pôsobiaci v&nbsp;Banskej Bystrici a&nbsp;Starých Horách.
            </p>
            <x-partials.picture titleSlug="ocds-002" side="left" dimensionSource="medium" columns="3" />

            <p>
                Dňa 18. júna 2017 bolo toto spoločenstvo v&nbsp;Detve (Svätej Terézie od Dieťaťa Ježiša a&nbsp;Svätej Tváre) slávnostne erigované - oficiálne založené. Od roku 2020 je jeho asistentom karmelitán Stanislav Miernik, farár v&nbsp;Starých Horách.
            </p>

            <p>
                Spoločenstvo svetského rádu sa v&nbsp;detvianskom kláštore bosých karmelitánok stretáva približne dvakrát do mesiaca na formačných stretnutiach a&nbsp;jedenkrát do mesiaca absolvujú členovia spoločne duchovnú obnovu so svojím asistentom. Svetský rád bosých karmelitánov v&nbsp;Detve má aktuálne 19 členov, z&nbsp;toho 13 členov s&nbsp;doživotnými a&nbsp;3&nbsp;s&nbsp;dočasnými prísľubmi a&nbsp;3&nbsp;sympatizantov (r. 2023).
            </p>
