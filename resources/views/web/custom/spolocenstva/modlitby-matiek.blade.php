                <x-partials.picture titleSlug="mm-001" side="right" dimensionSource="medium" columns="2" />

                <p>
                    Hnutie Modlitby matiek vzniklo v&nbsp;auguste 1995 na katolíckej charizmatickej konferencii „Nový úsvit“ vo Walsinghame v&nbsp;Anglicku, ktorú organizuje Komunita Princa Pokoja. Dve staré mamy Veronika a&nbsp;Sandra začali odovzdávať v&nbsp;modlitbe svoje deti do Ježišových rúk vo veľkej dôvere, že on môže zmeniť chod vecí. Napísali o&nbsp;tejto modlitbe len svojim známym. Bez akejkoľvek reklamy sa tieto modlitby rozšírili do celého sveta.
                </p>

                <p>
                    V apríli roku 1996 sa aj na Slovensku začali dve matky modliť za svoje deti podobným spôsobom. Onedlho vznikli viaceré skupinky Modlitieb matiek. Spiritualita hnutia je cesta úplného odovzdania sa. Matky sú pozvané žiť život podľa príkladu života Panny Márie, život modlitby, odovzdanosti, dôvery, lásky, trpezlivosti.
                </p>

                <x-partials.picture titleSlug="mm-003" side="left" dimensionSource="medium" columns="4" />

                <p>
                    Matky vkladajú svoje problémy do modlitby a&nbsp;prosia Pána Ježiša o&nbsp;jeho pohľad a&nbsp;riešenie. Matky odovzdávajú do rúk Pána Ježiša svoje deti s&nbsp;veľkou dôverou, že on ich miluje najviac a&nbsp;len on vidí aj v&nbsp;skrytosti.
                </p>

                <p>
                    Matky sa stretávajú v&nbsp;malých skupinkách dve až osem matiek, jedenkrát v&nbsp;týždni, vždy v&nbsp;ten istý deň a&nbsp;v tú istú hodinu. Tak vzniká nepretržitý reťazec modlitieb matiek na celom svete. Len na Slovensku sú modlitbami matiek pokryté všetky dni v&nbsp;týždni od rána od piatej hodiny do neskorého večera.
                </p>

                <p>
                    Dnes sa v&nbsp;našej krajine modlia tisíce matiek v&nbsp;malých skupinkách. Odpoveďou na <em>Modlitby matiek</em> bol vznik komunity Útecha, do ktorej patria okrem skupín <em>Modlitby matiek</em> aj skupinky <em>Modlitby otcov</em> a&nbsp;<em>Deti viery</em>.
                </p>

                <p>
                    Od roku 1997 sa na Slovensku koná každý rok na jar veľké stretnutie Modlitieb matiek. Matky z&nbsp;hnutia sa stretávajú na modlitbovom trojdní, ktoré sa v&nbsp;rovnakom čase koná na celom svete. Sú to modlitby odprosenia, odpustenia a&nbsp;modlitby chvál. Tieto modlitby sú otvorené pre všetkých ľudí dobrej vôle.
                </p>

                <x-partials.picture titleSlug="mm-005" side="right" dimensionSource="medium" columns="4" />

                <h5>Základné zásady:</h5>
                <ol class="lh-lg">
                    <li>
                        <strong>Neradiť:</strong>
                        <br>cez spoločnú modlitbu hľadať Božie riešenie na konkrétny problém tej-ktorej mamy.
                    </li>
                    <li>
                        <strong>Neohovárať:</strong>
                        <br>učiť sa milosrdenstvu voči blížnemu aj tak, že nehovoríme o&nbsp;zlých vlastnostiach druhých ľudí.
                    </li>
                    <li>
                        <strong>Nevynášať:</strong>
                        <br>dôverné veci, o&nbsp;ktorých hovoria matky na stretnutiach, sa inde nehovoria. Často ide o&nbsp;bolestné a&nbsp;osobné záležitosti, matky tak budujú vzájomnú dôveru.
                    </li>
                </ol>

                <p>
                    V&nbsp;našej farnosti sa matky stretávajú v&nbsp;sedemčlenných skupinách na spoločných modlitbách už viac ako 20 rokov. V&nbsp;súčasnosti sa na Modlitbách matiek stretáva vo farnosti sedem skupiniek (r.&nbsp;2022).
                </p>

                <p>
                    Okrem účasti na celoslovenskom stretnutí raz v&nbsp;roku, organizujú vo farnosti naše matky modlitbové trojdnie v&nbsp;Kostole sv. Františka z&nbsp;Assisi každý štvrťrok. Trojdnie Modlitieb matiek je prístupné všetkým záujemcom, je to vždy možnosť prihlásiť sa aj do tohto modlitbového spoločenstva.
                </p>

