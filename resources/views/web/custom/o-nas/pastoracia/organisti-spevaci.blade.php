                <x-partials.picture titleSlug="org-009" side="left" dimensionSource="medium" columns="2" />

                <p>
                    Okrem žalmistu poznáme tiež službu kantora - predspeváka, ktorý vedie a&nbsp;začína spevy liturgického zhromaždenia, zaisťuje striedavý spev, nahrádza chýbajúci zbor. Úlohu kantora nemusí zastávať nevyhnutne len organista. Význam služby kantora je daný potrebou sólového spevu niektorých častí v&nbsp;liturgii a&nbsp;živým spevom veriacich.
                </p>

                <p>
                    Rok po nástupe dekana Jána Štrbáňa sa vo farnosti v&nbsp;roku 1922 konal verejný konkurz na miesto organistu. Medzi viacerými uchádzačmi bol aj Martin Valent, ktorý pochádzal z&nbsp;Dvorian nad Nitrou. Keď sa predstavil hrou na organe, prítomní ho odmenili neutíchajúcim potleskom. Miesto organistu dostal a&nbsp;spolu s&nbsp;rodinou sa presťahoval do Detvy.
                </p>

                <x-partials.picture titleSlug="org-003" side="right" dimensionSource="medium" columns="4" />

                <p>
                    Martin Valent zbieral a&nbsp;notoval detvianske ľudové piesne, spolu s&nbsp;dekanom Štrbáňom sa intenzívne angažoval v&nbsp;sociálnej i&nbsp;kultúrnej sfére. Medzi ich významné počiny v&nbsp;oblasti hudobného folklóru patrí zbieranie vianočných ľudových piesní a&nbsp;ich publikovanie v&nbsp;malej brožúre.
                </p>

                <p>
                    Pokračovateľom Martina Valenta bol jeho syn František, aj on bol činný v&nbsp;kultúrnej oblasti. Stal sa vedúcim cirkevnej dychovej hudby a&nbsp;bol dlhoročným detvianskym organistom.
                </p>

                <p>
                    V&nbsp;našej farnosti v&nbsp;súčasnosti plnia úlohu organistu viacerí, ktorí sa podľa svojich pracovných možností usilujú o&nbsp;zabezpečenie organovej hudby počas bohoslužieb ako cez týždeň, tak zvlášť počas nedieľ a&nbsp;sviatočných dní. Služba organistu je veľmi potrebná pre uľahčenie a&nbsp;podnietenie spevu spoločenstva a&nbsp;pre hudobné dopĺňanie spevu k&nbsp;väčšej bohatosti a&nbsp;umeleckej účinnosti. Hru na organe je okrem sebavzdelávania možné študovať na základných umeleckých školách, konzervatóriách i&nbsp;na Vysokej škole múzických umení.
                </p>
