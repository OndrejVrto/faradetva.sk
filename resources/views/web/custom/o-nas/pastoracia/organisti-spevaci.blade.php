                <p>
                    Organista sprevádza liturgické spevy hrou na organe a&nbsp;v&nbsp;rámci liturgie môže v&nbsp;hre uplatniť aj vhodné diela organovej tvorby. Služba organistu spočíva v&nbsp;hudobnom sprievode tých častí bohoslužby, ktoré sú určené k&nbsp;spevu, a&nbsp;predovšetkým k&nbsp;sprevádzaniu spevu ľudu. Popri technickej stránke hry na organe by organisti mali byť oboznámení s&nbsp;liturgiou Cirkvi a&nbsp;uvedomovať si, že sú súčasťou liturgického zhromaždenia. Organista musí komunikovať s&nbsp;kňazom, ktorý celebruje svätú omšu, zároveň dbať na adekvátny výber piesní podľa jednotlivých liturgických slávení a&nbsp;môže viesť taktiež žalmistov, spevákov či spevácky zbor.
                </p>

                <x-partials.picture titleSlug="org-008" side="right" dimensionSource="medium" columns="3" />

                <p>
                    Služba organistu sa po prvýkrát objavuje v&nbsp;10. storočí, spolu s&nbsp;najstaršou zmienkou o&nbsp;inštalácii organu v&nbsp;kostole vo Winchesteri vo Veľkej Británii. Síce už v&nbsp;roku 757 daroval byzantský kráľ Konštantín V. franskému kráľovi Pipinovi organ, ale až oveľa neskôr sa hra na organe uplatnila v&nbsp;liturgii. Organ sa predtým používal bežne pri pohanských hrách, dostihoch, v&nbsp;arénach a&nbsp;divadlách.
                </p>

                <p>
                    V&nbsp;prvých troch storočiach nášho letopočtu, počas prenasledovania kresťanov, bola hra na organe úzko spätá s&nbsp;ich popravami v&nbsp;antických arénach. Rozmach používania tohto nástroja vo svätej omši priniesla v&nbsp;10. storočí až liturgická reforma benediktínskeho kláštora v&nbsp;Cluny vo Francúzsku.
                </p>

                <p>
                    Pri liturgii boli od staroveku Žalmy prednášané sólistom - žalmistom. Pôvodne Žalm spieval lektor, neskôr spevák - žalmista. Služba žalmistu má pôvod v&nbsp;Antiochii. Žalm je sólový, meditatívny spev a&nbsp;tvorí prirodzený kontrast k&nbsp;čítaniam. Mal by sa spievať, a&nbsp;to od ambony, pretože ide o&nbsp;prednes Božieho slova.
                </p>

