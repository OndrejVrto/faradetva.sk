                <p>
                    Umelecké cítenie detvianskych ľudí sa dostalo do sveta najmä prostredníctvom pestrých výšiviek a&nbsp;vyrezávaných krížov. Ďaleká minulosť Podpoľancov bola vždy spojená s&nbsp;tvrdou prácou s&nbsp;drevom, keďže ich predkovia sa usádzali v&nbsp;horách. Drevo ich sprevádzalo od kolísky až po hrob. Svojim zosnulým robili pomníky z&nbsp;dreva vo forme jednoduchých, ručne vyrezávaných, neskôr i&nbsp;polychrómovaných krížov. Od stredoveku sa kríž začal používať na označenie hrobu a&nbsp;stal sa symbolom viery, pomocou ktorej človek ľahšie prekonával tajomstvo smrti. Z&nbsp;typologického hľadiska ľudovej umeleckej výroby vyrezávaných krížov v&nbsp;Detve ide jednak o&nbsp;náhrobné kríže, jednak o&nbsp;veľké ústredné a&nbsp;prícestné kríže.
                </p>

                <x-partials.picture titleSlug="kriz-007" side="right" dimensionSource="medium" columns="3" />

                <p>
                    V&nbsp;18. storočí sa v&nbsp;Detve a&nbsp;okolí odrazil vývoj umeleckej úrovne drevených krížov v&nbsp;súvise s&nbsp;barokovým dekorativizmom sprevádzajúcim obdobie rekatolizácie, označujúc ho termínom zľudovený barok. Kríž si u&nbsp;zručných majstrov objednávali príbuzní zosnulého, pri pohrebe sa s&nbsp;úctou niesol pred rakvou a&nbsp;hneď sa osadil na hrob pri hlave zosnulého. Neprekvapuje preto, že vo výzdobe krížov prevažujú náboženské motívy. Popri univerzálne rozšírenej iniciále IH S&nbsp;(skratka mena Ježiš v&nbsp;gréčtine) sa objavuje motív viniča, srdca, Oltárnej sviatosti, kalicha s&nbsp;hostiou a&nbsp;sviec. Obvyklou súčasťou výzdoby boli tiež rastlinné ornamenty (kvety, halúzky), volúty, točené stĺpiky, solárne motívy, rovnako ako krátke nápisy s&nbsp;menom a&nbsp;dátami zosnulého.
                </p>

                <x-partials.picture titleSlug="kriz-033" side="left" dimensionSource="medium" columns="4" />

                <p>
                    Technologicky sa výzdoba zhotovovala obvyklým rezbárskym spôsobom - dekor sa predkreslil pomocou šablóny a&nbsp;vyrezal dlátom ako nízky plochý reliéf. Následne sa vymaľoval: podklad býval tmavý - čierny, modrý, neskôr tiež červenohnedý, ornament sa dotváral červenou, bielou, žltou, zelenou a&nbsp;modrou farbou. Umiestňoval sa obvykle na čelnú a&nbsp;bočné strany kríža. Kompozične sa členil hlbokými zárezmi a&nbsp;výstupkami priečnymi na telo kríža na dve - tri (prípadne viac) polí, z&nbsp;ktorých každé bolo motivicky samostatné. Pri novších krížoch z&nbsp;polovice 20. storočia úlohu členiaceho prvku dostal solárny motív a&nbsp;orámovaný monogram IHS. Ramená pri starších krížoch boli spravidla ukončené troma lalokmi, novšie okrem uvedenej formy majú pomerne často hladké zaoblené ramená. Telo Pána Ježiša a&nbsp;poloblúková strieška nad ramenami kríža sa vystrihovali z&nbsp;plechu.
                </p>

