                <p>
                    Kríže vyrábali rezbári z&nbsp;najtrvácnejšieho materiálu, t. j. z&nbsp;duba. Boli zastrešené ručne vyrobeným oblúkom z&nbsp;pozinkovaného plechu, ktorý bol pozahýnaný a&nbsp;vykrojený do ozdobných trojuholníkových, eliptických alebo kruhových tvarov. Najstaršie dochované kríže pochádzajú od neznámych výrobcov z&nbsp;konca 19. storočia. Vyznačujú sa širšou škálou námetov, vyváženou kompozíciou, väčšou plasticitou výzdoby a&nbsp;vynikajúcou rezbárskou technikou. Tradícia zhotovovania krížov pokračovala nepretržite až do súčasnosti. V&nbsp;polovici 20. storočia sa už možno stretnúť i&nbsp;s&nbsp;menami výrobcov - vďaka rešpektu k&nbsp;starším vzorom a&nbsp;tiež vďaka svojskému identifikovateľnému rukopisu treba spomenúť bratov Jána a&nbsp;Jozefa Fekiačovcov, ako aj Štefana Melicha. Dnes na tradície ich tvorby nadväzuje ďalšia generácia. Na novšej a&nbsp;súčasnej produkcii krížov vidieť snahu ísť do istej miery v&nbsp;intenciách tradičnej tvorby, ale snaha po dekorativizme v&nbsp;spojení s&nbsp;nebývalými technologickými a&nbsp;materiálovými možnosťami uberajú novej produkcii na presvedčivosti.
                </p>

                <x-partials.picture titleSlug="dkriz-010" side="righnt" dimensionSource="medium" columns="1" />

                <p>
                    Miestny cintorín, na ktorom sa nachádzajú staršie i&nbsp;novšie kríže, v&nbsp;20. storočí podstatne zmenil svoju tvár a&nbsp;kedysi obdivovaný les krížov nahradili nové moderné náhrobníky. Mesto Detva v&nbsp;spolupráci s&nbsp;Pamiatkovým ústavom v&nbsp;Banskej Bystrici, Podpolianskym múzeom, Miestnym odborom Matice slovenskej a&nbsp;s&nbsp;pomocou miestnych rezbárov preto podnikli kroky na záchranu časti nevyužívaných krížov z&nbsp;1. polovice 20. storočia. Kríže boli po obnove vystavené v&nbsp;stálej expozícii pod kalváriou tak, aby sa sprístupnili návštevníkom cintorína. Niektoré sú jednoduché, nezdobené, iné sú pestrofarebné. K&nbsp;historicky najhodnotnejším na tomto symbolickom cintoríne patria kríže bez polychrómie alebo s&nbsp;minimálnou farebnosťou. V&nbsp;20. rokoch 20. storočia vynikol výrobou pestro polychrómovaných krížov na objednávku Jozef Fekiač. Tvoril aj pre okolité obce, pre Slovenské národné múzeum v&nbsp;Martine a&nbsp;šesťdesiat krížov je na symbolickom cintoríne vo Vysokých Tatrách.
                </p>
                <p>
                    Kríže boli známe najprv ako náhrobné, neskôr sa podľa nich začali vyrábať i&nbsp;omnoho vyššie prícestné kríže. Stavali sa popri cestách, pri školách, na súkromných pozemkoch a&nbsp;pod. Náklady na ich zhotovenie znášala skupina osôb, ktorá mala záujem na tom, aby v&nbsp;ich oblasti kríž stál, alebo ich sponzoroval nejaký súkromný dobrodinec. Pri požehnaní kríža sa zvykla usporiadať pekná slávnosť za početnej účasti veriaceho ľudu.
                </p>
