                    Povrch oboch ramien kríža býval dekorovaný jednoduchou rezbárskou výzdobou pásových, cikcakovitých, zubovitých, archaických kruhových, hviezdicovitých a&nbsp;rozetových prvkov. Dekoratívny efekt umocňovala a&nbsp;do ľudovej polohy ho posúvala polychrómia. Vyskytli sa i&nbsp;kríže vytvorené na spôsob oltárika, resp. kaplnky s&nbsp;tŕňovou korunou a&nbsp;klincami vsadenou do kríža, alebo Pannou Máriou zasklenou vo vnútri kríža. Niektoré ornamenty sú totožné na výšivkách i&nbsp;krížoch a&nbsp;zdá sa, že muži vyrezávali to, čo ženy vyšívali alebo naopak. Náhrobné kríže obsahovali originálne, niekedy ťažko rozlúštiteľné nápisy.
