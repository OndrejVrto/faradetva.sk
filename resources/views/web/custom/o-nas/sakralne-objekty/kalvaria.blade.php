                <x-partials.picture titleSlug="kalv-061" side="left" dimensionSource="medium" columns="2" />

                <p>
                    Na starom (dolnom) cintoríne bol 19. júna 1703 položený základný kameň Kaplnky sv. Jozefa a&nbsp;19. marca 1704 ju miestny farár Juraj Majnó požehnal. Starý cintorín stál na konci mestečka a&nbsp;v&nbsp;roku 1718 bol cintorín pri kaplnke ohradený múrom. Počas pôsobenia farára Jána Štrbu (1829 - 1862) bola kaplnka pristavením zväčšená na menší kostolík. Taktiež veža bola o&nbsp;jedno poschodie zvýšená a&nbsp;prekrytá meďou. Patronát nad kostolíkom na cintoríne mali urbarialisti, neskôr okolo roku 1900 obec. Pretože sa oň obec málo starala, bol poškodený, a&nbsp;preto zatvorený. V&nbsp;jeho interiéri sa nachádzal kríž a&nbsp;3&nbsp;kamenné sochy. Pre havarijný stav kostolíka v&nbsp;ňom biskup Wolfgang Radnai zakázal v&nbsp;roku 1909 konať bohoslužby. Na návrh farára Antona Kúdelku dovolil postaviť krížovú cestu a&nbsp;novú kaplnku na vrchu kalvárie, ktorá stojí dodnes. Zo skál rozobratého Kostola sv. Jozefa a&nbsp;zo zvyšných skál pri stavbe kalvárie bol cintorín čiastočne ohradený novým múrom.
                </p>

                <x-partials.picture titleSlug="kalv-054" side="right" dimensionSource="medium" columns="3" />

                <p>
                    Po Veľkej noci v&nbsp;roku 1910 sa začalo so stavbou kalvárie na mieste, kde pôvodne stáli tri kríže a&nbsp;miestny ľud to už kalváriou aj nazýval. Zásluhou šikovných murárov a&nbsp;tesárov zo Seliec bola od apríla do septembra krížová cesta hotová. Skaly nalámali farníci v&nbsp;kameňolome  v&nbsp;„Rohoch“ a&nbsp;priviezli na vozoch. Stavba Kalvárie bola financovaná zo základiny Kostola sv. Jozefa, základiny cintorína, milodarov obetavých farníkov. Kalváriu 17. septembra 1910 slávnostne požehnal kňaz Rádu sv. Františka, bývalý detviansky kaplán Mansuetus Prikaszky.
                </p>

                <x-partials.picture titleSlug="kalv-076" side="left" dimensionSource="medium" columns="3" />

                <p>
                    Kalváriu tvorí 14 zastavení krížovej cesty a&nbsp;Kaplnka Sedembolestnej Panny Márie. Zastavenia sú malé murované hranolové stavby s&nbsp;obdĺžnikovým pôdorysom, odstupňovaným soklom, zastrešené sedlovou strieškou. Presklené niky, ukončené lomeným oblúkom vypĺňajú štukové polychrómované reliéfy výjavov krížovej cesty. Päť zo štrnástich zastavení stojí pozdĺž chodníka, ktorý vedie cez cintorín. Ostatné zastavenia pokračujú serpentínovito zalesneným malým kopcom až na jeho vrchol, kde stojí kaplnka.
                </p>

                <p>
                    Kaplnka Sedembolestnej Panny Márie bola postavená v&nbsp;historizujúcom slohu. Má jednoduchý obdĺžnikový pôdorys s&nbsp;polygonálnym uzáverom, uprostred ktorého je umiestnená štuková socha Piety. Čelnú fasádu ukončuje trojuholníkový štít s&nbsp;drevenou zvoničkou. Interiér je zaklenutý valenou klenbou. Umelecky sú zaujímavé vitrážové okná s&nbsp;postavami sv. Antona Paduánskeho, sv. Jozefa, sv. Štefana, kráľa, a&nbsp;sv. Anny vyučujúcej Pannu Máriu. Pri kaplnke stojí vyrezávaný detviansky kríž od miestneho ľudového rezbára Jána Fekiača z&nbsp;roku 1974.
                </p>

                <x-partials.picture titleSlug="kalv-032" side="left" dimensionSource="medium" columns="4" />

                <p>
                    Približne v&nbsp;strede kalvárskeho kopca stojí štvorfigurálne polychrómované štukové súsošie ukrižovania. Je situované na vysokom podstavci a&nbsp;prekryté stanovou strieškou na nárožných pilieroch. Štukové reliéfy krížovej cesty a&nbsp;sochy sú vyhotovené firmou Bayerische Kunstanstalz z&nbsp;Mníchova. Na vežičku kalvárskeho kostolíka bol zadovážený 66 kg vážiaci zvon, ktorý počas prvej svetovej vojny spolu s&nbsp;ostatnými zvonmi farnosti rekvírovali. V&nbsp;roku 1935 vdova Vagačová darovala pre kaplnku na kalvárii nový zvon. Obrazy Krista Pána a&nbsp;Panny Márie darovala v&nbsp;roku 1911 hostinská z&nbsp;hriňovskej sklenej huty. Na kalvárii bolo povolené slúžiť svätú omšu v&nbsp;tie nedele, ktoré nasledovali po sviatkoch nájdenia a&nbsp;povýšenia Svätého kríža.
                </p>

