                <x-partials.picture titleSlug="kalv-037" side="right" dimensionSource="medium" columns="3" />

                <p>
                    V&nbsp;roku 1965 sa kalvária renovovala. Všetky zastavenia krížovej cesty aj s&nbsp;kaplnkou sa podľa vdp. Martina Láclavíka nachádzali v&nbsp;dezolátnom stave, boli prestavené a&nbsp;obhodené brizolitom. V&nbsp;roku 2007 bola ku kaplnke pristavaná strieška s&nbsp;plošinou, aby sa vonku mohla slúžiť výročná svätá omša, ktorá býva na kalvárii po období komunistickej totality opäť vždy v&nbsp;septembrovú nedeľu v&nbsp;blízkosti sviatku povýšenia Svätého kríža (14. septembra).
                </p>

                <p>
                    Pri príležitosti 100.&nbsp;výročia vybudovania kalvárie sa zrealizovala jej kompletná rekonštrukcia. Jednotlivé zastavenia krížovej cesty boli ostierkované a&nbsp;premaľované, piate zastavenie bolo zo statických dôvodov premiestené na pravú stranu chodníka vedúceho cez cintorín. Veľkú zásluhu na rekonštrukcii mal pán Jozef Žubrietovský. Reliéfy krížovej cesty boli kompletne zreštaurované farníkmi Milanom Ďuricom a&nbsp;Tomášom Puškárom. Kalváriu nanovo požehnal biskup Rudolf Baláž 12.&nbsp;septembra 2010.
                </p>

