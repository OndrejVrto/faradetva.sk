        <x-web.page.subsection title="Božie muky">

            <x-web.page.text-segment animation="fromright">
                <p>
                    Božie muky sú drobné sakrálne objekty ľudovej architektúry vo forme dreveného alebo murovaného stĺpa s&nbsp;výklenkom pre sošku, alebo obraz. Najčastejšie znázorňovali pašiové výjavy Ježišovho utrpenia. Do výklenkov sa umiestňovali reliéfy, obrazy alebo sošky svätých. Takéto menšie prícestné kaplnky vznikali najmä ako pamätníky šťastných či naopak tragických udalostí v&nbsp;živote rodín alebo aj jednotlivcov. Boli to napríklad morové epidémie, uzdravenia z&nbsp;choroby, prežitie nehody, uctenie zomrelých, uzmierenie sporov a&nbsp;podobne.
                </p>

                <p>
