        <x-web.page.subsection title="Kaplnka Panny Márie, Matky ustavičnej pomoci - Krné">
            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="krn-001" side="left" dimensionSource="medium" columns="4" />

                <p>
                    Koncom apríla 2007 bol dekanom Ľubošom Sabolom požehnaný pozemok v&nbsp;Krnom, na stavbu novej kaplnky vo farnosti. V&nbsp;priebehu jedného roka si obyvatelia Krného pod vedením pána Jozefa Krnáča svojpomocne postavili Kaplnku Panny Márie, Matky ustavičnej pomoci. Slávnostné požehnanie kaplnky dekanom Sabolom sa uskutočnilo 27. júla 2008. Každoročne býva pri kaplnke slávená výročná svätá omša, v&nbsp;poslednú júnovú alebo prvú júlovú nedeľu.
                </p>
                <p>
                    Ikona Matky ustavičnej pomoci patrí dnes k&nbsp;známym obrazom Panny Márie. Pápež Pius IX. zveril obraz Matky ustavičnej pomoci v&nbsp;roku 1866 redemptoristom. Táto ikona vznikla na Kréte v&nbsp;15. storočí a&nbsp;300 rokov bola uctievaná v&nbsp;rímskom Kostole sv. Matúša. Od 26. apríla 1866 sa nachádza v&nbsp;Kostole sv. Alfonza v&nbsp;Ríme. Mariánska úcta sa vďaka redemptoristom upevňovala aj úctou k&nbsp;Matke ustavičnej pomoci, ktorá sa rozšírila takmer po celom svete.
                </p>

                <x-partials.picture titleSlug="krn-003" side="right" dimensionSource="medium" columns="5" />

                <p>
                    Na Slovensko sa táto úcta dostala prostredníctvom českých redemptoristov po roku 1921, keď sa usadili v&nbsp;Podolínci a&nbsp;v Kostolnej-Záriečí. Ikona Matky ustavičnej pomoci sa nachádza vo všetkých rehoľných kostoloch redemptoristov. S&nbsp;touto úctou je spojená aj tzv. ustavičná novéna, ktorú sa veriaci modlia v&nbsp;stredu alebo v&nbsp;sobotu. Počas novény odznievajú poďakovania a&nbsp;prosby, ktoré ľudia Matke ustavičnej pomoci napíšu. Redemptoristi vedú k&nbsp;tejto úcte, pretože Panna Mária nám chce ustavične a&nbsp;účinne pomáhať.
                </p>
                <p>
                    Napriek tomu, že Mária je najväčšou postavou obrazu, nie je jeho centrom. V&nbsp;geometrickom strede ikony sa stretajú ruky Márie a&nbsp;Ježiša. Ježišove malé ruky, ktorými sa drží pravej ruky Matky, symbolizujú hlavné posolstvo ikony: Ježiš aj v&nbsp;nebi vkladá do rúk Márie všetky milosti, aby nám ich rozdeľovala. Redemptoristi na celom svete slávia sviatok Panny Márie, Matky ustavičnej pomoci, 27. júna.
                </p>
