                </p>

                <p>
                    Popri mužských vetvách sa rozšíril aj druhý, čiže ženský rád sv. Františka, nazývaný podľa zakladateľky sv. Kláry. Keďže klarisky žili uzavretým kláštorným životom, nedosiahli taký počet ako apoštolsky činní menší bratia. No ostali obdivuhodne verné pôvodným ideálom sv. Františka a&nbsp;svojou duchovnosťou sa stali požehnaním pre celú Cirkev.
                </p>

                <p>
                    Tretí rád sv. Františka združuje laických veriacich. V&nbsp;priebehu storočí sa v&nbsp;rámci tretieho rádu vyvinulo mnoho mužských a&nbsp;najmä ženských rehoľných spoločenstiev, ktoré sa hlásia k&nbsp;duchovnému dedičstvu sv. Františka.
                </p>

                <p>
                    Pozemský život sv. Františka sa síce skončil večer 3. októbra 1226, čo sa ale podľa vtedajšieho počítania času počítalo už k&nbsp;nasledujúcemu dňu, preto sa jeho sviatok slávi 4. októbra. Za svätého bol vyhlásený pápežom Gregorom IX. v&nbsp;roku 1228. Pápež Pius XII. ho v&nbsp;roku 1939 vyhlásil za patróna Talianska a&nbsp;v&nbsp;roku 1980 bol sv. Jánom Pavlom II. vyhlásený za patróna ekológov.
                </p>

