                    Sviatok Božího ťela je cirkevne prikázaným sviatkom s&nbsp;pohyblivým dátumom. Slávi sa vo štvrtok po slávnosti Najsvätejšej Trojice (prvá nedeľa po skončení veľkonočného obdobia). Súčasťou bohoslužieb býva v&nbsp;Detve od nepamäti procesia sprevádzaná dychovou hudbou. Na štyroch miestach v&nbsp;uliciach mesta okolo kostola urobili miestne ženy zelené oltáriky - koľibki, pri ktorých sa udeľovalo eucharistické požehnanie. Prvá bola na dolnom konci pri feštrovi, druhá pred Lašouskím, treťa pri Floriánkovi a&nbsp;štvrtá pred Vagačovci. (Svedectvo ženy narodenej v&nbsp;roku 1929). Kňaz s&nbsp;hostiou kráčal pod baldachýnom, ktorý niesli štyria chlapi v&nbsp;krojoch. Mládenci niesli vyzdobené zástavy a&nbsp;z&nbsp;nich visiace stuhy niesli dievky oblečené v&nbsp;bielych krojoch. Malé dievčatá posýpali cestu pred kňazom kvetnými lupeňmi, tieto zvykli byť od polovice 20. storočia oblečené v&nbsp;šatách z&nbsp;prvého svätého prijímania.
