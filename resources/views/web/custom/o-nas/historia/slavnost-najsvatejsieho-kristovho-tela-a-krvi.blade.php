                <p>
                    Istý český kňaz, Peter z&nbsp;Prahy, pricestoval do Talianska v&nbsp;roku 1264, aby sa zúčastnil na audiencii u&nbsp;pápeža Urbana IV., ktorý sa v&nbsp;tom čase cez letné mesiace nachádzal v&nbsp;Orviete. Spolu s&nbsp;ním tam bol aj sv. Tomáš Akvinský a&nbsp;mnohí ďalší teológovia a&nbsp;kardináli. Po stretnutí s&nbsp;pápežom sa kňaz Peter cestou späť do Čiech zastavil v&nbsp;Bolsene, kde slávil svätú omšu v&nbsp;Kostole sv. Kristíny. Počas vyslovenia slov premenenia sa udial zázrak, ktorý popisuje kamenná tabuľa umiestnená na tomto mieste: „Zrazu sa hostia viditeľne premenila na skutočné mäso pokropené červenou krvou, okrem časti, za ktorú ju držal kňaz v&nbsp;rukách: nemohlo sa to udiať bez tajomstva, ale stalo sa tak preto, aby aj ostatní mohli vidieť, že je to naozaj hostia, ktorú drží v&nbsp;rukách celebrujúci kňaz nad kalichom.“
                </p>

                <p>
                    Vďaka tomuto zázraku posilnil Pán Boh vieru kňaza, ktorý napriek známej zbožnosti a&nbsp;morálnemu životu, mával často pochybnosti ohľadom skutočnej prítomnosti Krista pod spôsobom premeneného chleba a&nbsp;vína. Zvesť o&nbsp;zázraku sa ihneď rozšírila a&nbsp;tak samotný pápež, ako aj sv. Tomáš Akvinský, mohli okamžite potvrdiť tento zázrak. Po dôkladnom preskúmaní pápež Urban IV. povolil jeho uctievanie a&nbsp;rozhodol o&nbsp;rozšírení sviatku Božieho tela, ktorý bol predtým lokálnym sviatkom diecézy Liegi, na celú Katolícku cirkev: „Aby táto vznešená a&nbsp;úctyhodná sviatosť bola pre všetkých pamiatkou mimoriadnej lásky Boha k&nbsp;nám.“
                </p>

                <p>
                    Medzi eucharistickými procesiami má osobitný význam a&nbsp;dôležitosť v&nbsp;pastoračnom živote farnosti alebo mesta procesia, ktorá sa koná každý rok na slávnosť Kristovho tela a&nbsp;krvi alebo v&nbsp;iný vhodný deň pred touto slávnosťou alebo po nej. Eucharistické procesie sa usporiadavajú podľa miestnych zvykov, či už ide o&nbsp;výzdobu verejných priestranstiev a&nbsp;ulíc, alebo o&nbsp;usporiadanie účastníkov procesie. Podľa zvyklostí sa môžu počas procesie, keď kňaz kráča verejným priestranstvom pod baldachýnom, nesúc Oltárnu sviatosť v&nbsp;monštrancii, urobiť zastavenia s&nbsp;eucharistickým požehnaním. Na konci procesie sa udelí záverečné požehnanie s&nbsp;Najsvätejšou sviatosťou v&nbsp;kostole, v&nbsp;ktorom sa Sviatosť uloží.
                </p>

