                <p>
                    Štedrovečerné jedlá sa do polovice 20. storočia takmer nemenili a zachovali si pôstny charakter. Boli to predovšetkým: oblátky s cesnakom a medom, opekance s makom, kapustová polievka s hríbmi, chlieb a koláče z kysnutého cesta. Jedávalo sa zo spoločnej misky, čo symbolizovalo súdržnosť rodiny. Pripíjalo sa <em>hriatym</em> bez tuku, okrem toho sa pripravil nápoj z varených sušených hrušiek. Večera sa zakončila modlitbou a poďakovaním za konzumované dary. Omrviny a zvyšky zo štedrovečerného stola sa odkladali a používali pri domácom liečení ľudí aj dobytka.
                </p>
                <p>
                    Večer sa chodilo po spievaní a vinšovaní. Osobitnú pozornosť si zasluhuje pochôdzkové chodenie s betlehemom, ktoré je v Detve, ale aj v Hriňovej a Detvianskej Hute dodnes súčasťou živej neprerušenej tradície. <em>Betľehemci začínali na Štedrí deň po večeri a šeci sa zišli na pounočnej. Kubovia ostaľi pret kostolom... Chodievali Dolňickí, Horňickí, Laštekári, Skliarovčaňia, Kostolňaňia, Piešťanci.</em> Predvádzali ľudovú vianočnú hru s témou narodenia Ježiška. Počet betlehemcov nebýval všade rovnaký. Hlavné postavy hry sú: <em>Kresťan (anjel)</em>, bača a pastieri - valasi: <em>Fedor, Stacho, Ivan, Bača - Kubo - Starý</em>. Kubo chodil inkognito v maske z ovčej kožušiny. Dôležitou rekvizitou bol figurálny <em>betľehemček</em> a <em>štrngalá</em> (kyjaky). Betlehemcov v rodinách netrpezlivo očakávali, za pochôdzku ich pohostili a odmenili peniazmi.
                </p>
