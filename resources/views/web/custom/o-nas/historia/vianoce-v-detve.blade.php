            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="kronika-299" side="right" dimensionSource="medium" columns="5" />

                <p>
                    Pokračovateľom Martina Valenta bol jeho syn František, aj on bol činný v kultúrnej oblasti. Stal sa vedúcim cirkevnej dychovej hudby a pôvodné otcove notové zápisy poopravoval a transponoval. Vďaka nemu bol opätovne vydaný Vianočný detviansky spevník. Polnočná svätá omša v Detve je okrem duchovného aj umeleckým zážitkom pre milovníkov ľudovej kultúry. V kostole je mnoho ľudí oblečených v detvianskych zimných krojoch, nechýbajú betlehemci a niekoľko <em>Kubov</em>.
                </p>

                <p>
                    Svätá omša sa začína hrou na fujare, po ktorej bubon a motív kukučky na organe dvanásťkrát odrátajú odbitie polnoci. Okrem Žalmu a Alelujového spevu sa vo svätej omši spievajú vianočné koledy, ktoré boli v Detve zozbierané pred desiatkami rokov. Toto dedičstvo sa domáci snažia zachovávať, a preto mnohí poznajú tieto piesne naspamäť a spievajú ich s veľkým oduševnením.
                </p>

                <p>
                    Polnočná svätá omša popretkávaná ľudovými koledami sa traduje už niekoľko desiatok rokov. Kňazi, ktorí v Detve pôsobili po Jánovi Štrbáňovi, akceptovali tradície, ktoré sú v miestnych ľuďoch silno zakorenené. Ľudové koledy sú im totiž stále veľmi blízke. Oproti liturgickým piesňam majú jednoduchšie texty a rytmus, jednoduchšie harmonické melódie, väčšinou s úzkym rozsahom, a preto sú veľmi ľahko zvládnuteľné aj pre menej zdatných spevákov.
                </p>

                <x-partials.picture titleSlug="vian-009" side="left" dimensionSource="medium" columns="3" />

                <p>
                    Nemalú funkciu v uchovávaní kolied zohrávajú miestne detské folklórne kolektívy. Vďaka ich práci aj súčasné deti poznajú vianočné detvianske ľudové piesne. A tak môžeme na polnočnej svätej omši pozorovať spoločný spev viacerých generácií. Detí, mládežníkov, dospelých i veriacich najstaršej generácie, čo je v súčasnosti až jedinečné.
                </p>

                <p>
                    Na <em>Božuo naroďeňia</em> (25. decembra) - Detvania podľa K. A. Medveckého - <em>na Boženarodenia nič nerobia, ani nevaria, ani statok nečešú, z maštalí nekydajú, iba čo ho nachovajú, ba dade v tento deň ani ohňa nekladú.</em> Ide o prvý sviatok vianočný, ktorý sa považuje za jeden z najväčších sviatkov. V tento deň sa svätilo, nesmelo sa robiť, hlasno hovoriť, vadiť sa, búchať ani chodiť po cudzích domoch, zákaz platil pre dospelých aj pre deti.
                </p>

                <p>
                    Na druhý sviatok vianočný, <em>na Šťefana</em> (26. decembra), sa podľa staršej tradície chodilo <em>po chrene</em>. Neskôr sa pochôdzke hovorilo <em>chodiť po šibaňí</em>. Chlapci a mládenci šibali dievčatá brezovými prútmi - <em>chrenovcami</em>, ktoré po domoch roznášali pastieri. V medzivojnovom období a neskôr, keď už pastieri prúty neroznášali, si ich museli obstarať sami šibači, ktorí po príchode do domu najskôr zavinšovali a za šibačku ich domáci odmenili. Vinšovanie si udržalo pôvodný charakter približne do polovice 20. storočia.
                </p>

                <x-partials.picture titleSlug="kronika-712" side="right" dimensionSource="medium" columns="5" />

                <p>
                    Na Silvestra - <em>Starí rok</em> (31. decembra) ráno chlapci chodili s oceľou, tento zvyk neskôr zanikol. Večera prebieha bez poverových úkonov a zákazov. Oheň, strieľanie, zvonenie a praskanie bičom malo chrániť pred negatívnymi silami. Po bohoslužbe v kostole farár verejne hodnotil cirkevný rok a hospodárenie farnosti.
                </p>

                <p>
                    Na <em>Noví rok</em> (1. januára) sa ráno vstávalo zavčasu a každý sa umyl v studenej vode, aby bol po celý rok svižný, zdravý a prvý v práci. Celý deň sa svätilo, dopoludnie patrilo návšteve kostola. <em>Tri kráľe</em> (6. januára) sú akoby posledným dňom vianočného cyklu. V tento deň sa v Detve konala trojkráľová koleda.
                </p>

                <p>
                    Kým podľa staršej tradície chodili po domoch masky: kňaz, anjel a čert, pričom trojkráľovou kriedou a vodou požehnávali domy, v neskoršom období už masky nadobudli podobu biblických postáv troch kráľov: Gašpara, Melichara a Baltazára. Traja králi po príchode do domu zaspievali koledu, pokropili dom svätenou vodou a nad vchodové dvere domu a nad veraje maštale napísali kriedou letopočet a písmená C+M+B (Christus mansionem benedicat - Kristus nech žehná tento príbytok).
                </p>

                <p>
                    Neodmysliteľnou súčasťou sviatkov, aj vzhľadom na vierovyznanie obyvateľstva, bola účasť na bohoslužbách. Do kostola v Detve prichádzali ľudia nielen z Detvy, jej lazov a osád, ale aj z okolitých obcí: Stožka, Kriváňa, Korytárok a susedných osád Hriňovej.
                </p>

