                <p>
                    Keď v roku 1896 budovu detvianskej obecnej školy prevzal štát, vyplatil Cirkvi odškodné na zaobstaranie bytu pre organistu, ktorý dovtedy v škole býval. Organistov byt sa potom zriadil v novom chudobinci. Okrem tohto bytu sa tam nachádzal i byt pre kostolníka, veľká prednášková, resp. divadelná sieň s javiskom, miestnosť pre nacvičovanie cirkevnej hudby (trubačov), ktorá hrávala pri procesiách a na významnejšie sviatky, a štyri samostatné izby pre chudobných. Začiatkom 20. storočia bývali v chudobinci traja chlapi a päť žien. Živobytie si obstarávali žobraním, niektorí sa podieľali na čistení kostola a jeden z nich zastupoval zvonára.
                </p>

                <x-partials.picture titleSlug="chud-025" side="right" dimensionSource="medium" columns="3"/>

                <p>
                    1. júla 1956 bola uzavretá zmluva o bezplatnom prenájme chudobinca s Miestnym národným výborom (MNV). Zmluvu schválil Krajský národný výbor a Biskupský úrad. MNV urobil z miestností veľkú sálu. V budove ponechali izbu pre zvonára i rechtorský byt. Štyri menšie miestnosti mal v prenájme Ústav národného zdravia. Javisko a veľká sála chudobinca sa začali využívať na divadelné predstavenia ochotníckeho divadla. Sála bola asi pre 150 divákov, ale neraz sa v nej tiesnilo aj 300 ľudí. Pre veľký záujem sa hrávalo aj v nedeľu o 13-tej hodine pre lazníkov, ktorí prišli do kostola. V roku 1960 bol chudobinec rozhodnutím Okresného národného výboru vo Zvolene bez náhrady vzatý Cirkvi a daný do správy MNV Detva. Dekan Jozef Závodský v roku 1992 požiadal o jeho navrátenie, odvolávajúc sa na skutočnosť, že kostol ako kultúrna pamiatka nemá okrem základov, na ktorých stojí, žiadne ochranné pásmo.
                </p>
