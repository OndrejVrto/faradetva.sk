                <p>
                    Katolícka cirkev v Detve mala výdavky nielen na kostol a faru, ale aj na sociálne ustanovizne, ktoré sa nachádzali v jej správe. Karol Anton Medvecký ich nazýva „ustanovizne kresťanskej lásky“. Medzi ne patril „špitál“, t. j. cirkevný chudobinec, resp. starobinec a o čosi neskôr postavený sirotinec. Farnosť v Detve plnila vďaka miestnemu chudobincu aj zdravotnícko-charitatívnu funkciu. Charitatívne zariadenie poskytovalo strechu nad hlavou a stravu pre najbiednejších v okolí už od prvej polovice 18. storočia.
                </p>

                <x-partials.picture titleSlug="chud-006" side="right" dimensionSource="medium" columns="3"/>

                <p>
                    Na pokrytie bežných výdavkov chudobienca mal detviansky farár príjmy z almužien veriacich. Počiatok a ani zakladateľ chudobinca nie je známy. V roku 1748 sa po prvýkrát vyskytuje kniha účtov chudobinca. V roku 1784 nariadila Zvolenská stolica vykonať súpis špitálskeho majetku. Bola to drevená budova, ktorá si už v roku 1811 vyžadovala totálnu prestavbu.
                    V roku 1819 z nej boli špitálnici vysťahovaní, lebo sa so stavbou už nemohlo odkladať. V najväčšej miere na ňu finančne prispel detviansky rodák Lepieš, sumou 500 zlatých. Veriaci zadarmo poskytli záprah a prácu, kameň dodala Zvolenská stolica. Farár Jozef Moczay opätovne prosil panstvo, aby na stavbu darovalo aspoň potrebné drevo, no odpoveď bola zamietavá. Čo chýbalo, doplatil farár s pomocou príspevkov veriacich. Celá stavba stála 994 zlatých.
                </p>

                <x-partials.picture titleSlug="chud-020" side="left" dimensionSource="medium" columns="4"/>

                <p>
                    Špitál mal dve priestranné izby. Prvá, s príručnou komôrkou, slúžila ako byt šiestim vdovám. Druhá izba bola pre šafára (správcu) a na zohriatie sa lazníkov, keď v zime schádzali z lazov do kostola. V predsieni bolo pre všetkých veľké ohnisko. K špitálu patrila aj kuchynská záhrada. Špitálničky si varili pri spoločnom ohnisku každá zvlášť. Veriaci ich podporovali štedrými milodarmi. Obyvateľov, ženy i mužov, do špitála prijímal farár a dával to na vedomie obecnej vrchnosti.
                </p>

                <p>
                    V roku 1888 bol špitál znovu opravený, rovnako aj v roku 1903, keď vďaka starostlivosti farského administrátora Ignáca Lúckeho dostal nové náradie. O sedem rokov neskôr boli na špitáli vykonané drobné opravy (pribitá nová lepenka, nové rámy okien na hornej izbe a o čosi neskôr aj na dolnej izbe). Farár Anton Kúdelka dal záhradu pri špitáli premerať sudcovi, lebo ju obec zmenšila. Podľa nového výmeru záhradu oplotili a dali do užívania kostolníkovi. Zregulovali i koryto potoka, pretože podmýval základy špitála. V roku 1931 dal farár Ján Štrbáň s povolením banskobystrického biskupstva starý špitál zbúrať a na jeho mieste postavili dodnes existujúcu budovu za 130 000,- Kč. Pôvodný plán farára Štrbáňa bol taký, aby v chudobinci bol aj sirotinec. Tento projekt však nebol úradmi podporovaný a nemohol sa tak zrealizovať. 18. októbra 1931 novostavbu chudobienca požehnal dekan Karol Majthán, očovský farár.
                </p>
