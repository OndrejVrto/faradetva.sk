        {{-- Jozef Závodský --}}

        <x-web.page.subsection>
            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="zav-003" side="left" dimensionSource="medium" columns="2"/>

                <h6 class="text-church-template">* <span class="ps-1 pe-2">30. 1. 1927 </span>Sučany (okr. Martin)</h6>
                <h6 class="text-church-template">† <span class="ps-1 pe-2">17. 2. 1993 </span>Handlová (okr. Prievidza)</h6>

                <p>
                    Jozef Závodský sa narodil 30. januára 1927 v&nbsp;Sučanoch, rodičom Andrejovi a&nbsp;Márii rod. Hromadovej. V&nbsp;rokoch 1933 - 1938 navštevoval ľudovú školu v&nbsp;Sučanoch. Prvú triedu gymnázia absolvoval v&nbsp;Martine, odtiaľ v&nbsp;roku 1939 prestúpil na katolícke gymnázium do Kláštora pod Znievom. V&nbsp;roku 1944 pri ústupe nemeckých vojsk naším územím boli Nemci konfrontovaní v&nbsp;Sučanoch činnosťou slovenských partizánov. Pretože tam partizáni postrieľali diplomatickú delegáciu prechádzajúcu z&nbsp;Rumunska do Berlína, nemeckí vojaci zadržali približne 200 miestnych mužov. Obvinených od 16 do 60 rokov, so spolupráce s&nbsp;partizánmi, odviedli do Vrútok a&nbsp;následne koncom septembra odvliekli do zajateckých táborov v&nbsp;Nemecku. Medzi obvinenými bol tiež Jozef Závodský, ktorý tak medzi zajatcami v&nbsp;tábore strávil deväť mesiacov. Po oslobodení Američanmi sa 30. júna 1945 vrátil domov a&nbsp;v nasledujúcom školskom roku dokončil v&nbsp;Kláštore pod Znievom stredoškolské štúdium. Po maturite v&nbsp;roku 1946 smerovali jeho ďalšie kroky do Kňazského seminára sv. Karola Boromejského v&nbsp;Banskej Bystrici.
                </p>
            </x-web.page.text-segment>
            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="zav-002" side="right" dimensionSource="medium" columns="2"/>

                <p>
                    Nástupom komunistickej moci v&nbsp;krajine sa začali po roku 1948 postupné represálie voči Cirkvi. Po smrti biskupa Andreja Škrábika 8. januára 1950 bol totalitnou mocou dosadený do vedenia diecézy kňaz trnavskej apoštolskej administratúry Ján Dechet, ktorý bol na základe prijatia cirkevného úradu od svetskej moci Vatikánom exkomunikovaný. Voči jeho osobe zaujali predstavení a&nbsp;bohoslovci kňazského seminára rázny odpor, preto krátko po Dechetovej inštalácii v&nbsp;katedrále boli predstavení seminára zaistení do vyšetrovacej väzby a&nbsp;seminaristi poslaní domov. Do seminárnych lavíc v&nbsp;Banskej Bystrici sa už nikdy viac nevrátili, pretože vládnym nariadením boli 14. júla 1950 zrušené všetky diecézne semináre a&nbsp;bohoslovecké štúdium bolo sústredené pre celé Slovensko len do Bratislavy. Viacerí banskobystrickí bohoslovci boli poslaní do vojenských Pomocných technických práporov (PTP), kde v&nbsp;rokoch 1950 - 1953 bol aj Jozef Závodský.
                </p>

                <p>
                    V&nbsp;roku 1953 začal pracovať ako brigádnik v&nbsp;národnom podniku Stavoindustria v&nbsp;Martine a&nbsp;od roku 1954 pokračoval ďalej ako brigádnik vo Váhostave v&nbsp;Sučanoch. V&nbsp;roku 1956 sa zamestnal v&nbsp;Štátnych majetkoch v&nbsp;Martine a&nbsp;od roku 1958 pracoval v&nbsp;Drevine Turany. V&nbsp;rokoch 1958 - 1960 absolvoval stredoškolskú nadstavbu na Priemyselnej škole elektrotechnickej v&nbsp;Martine. Za chrabrosť a&nbsp;zásluhy počas druhej svetovej vojny mu bol udelený Československý vojnový kríž. Vrátiť sa do kňazského seminára a&nbsp;dokončiť teologické štúdium bolo Závodskému umožnené až v&nbsp;roku 1966. Aby sa mohol stať kňazom, musel cez mnohé skúšky a&nbsp;utrpenia, vynaložiť veľké úsilie a&nbsp;odhodlanie.
                </p>
            </x-web.page.text-segment>

