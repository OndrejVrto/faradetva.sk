                <p>
                    V&nbsp;roku 1986 vrcholili snahy zo strany totalitného režimu o&nbsp;odvolanie a&nbsp;preloženie dekana Závodského, pretože nevyhovoval jeho ideológii. Hľadali sa rôzne dôvody ako napríklad:
                </p>

                <ul>
                    <li>údajne na pohrebnej kázni, keď pochovával tri dievčatá, ktoré zahynuli pri autohavárii v&nbsp;Prahe počas prázdninovej brigády, hovoril proti socialistickým brigádam;</li>
                    <li>počas primícií detvianskeho rodáka Jozefa Strapku, išiel z&nbsp;jeho rodného domu do kostola  slávnostný a&nbsp;početný krojovaný sprievod sprevádzaný cirkevnou hudbou;</li>
                    <li>v máji bolo na birmovke 600 detí;</li>
                    <li>tŕňom v&nbsp;oku bolo i&nbsp;postavenie novej farskej budovy, svojpomocne, bez finančného  príspevku štátu a&nbsp;za pomoci veriacich.</li>
                </ul>

                <p>
                    Dekan sa obhajoval, že on si robí len svoju kňazskú robotu a&nbsp;agitovať na birmovku ani na primície nechodil. Svojho kňaza sa zastali aj veriaci. Nakoniec sa odvolanie neuskutočnilo a&nbsp;prisluhovači totalitného režimu sa možno i&nbsp;obávali rozbúrenej verejnej mienky.
                </p>
