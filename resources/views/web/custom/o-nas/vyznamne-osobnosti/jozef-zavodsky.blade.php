                <p>
                    V&nbsp;roku 1988 sa uskutočnili oslavy 350. výročia založenia Detvy. Dekan Závodský chcel nadviazať na oslavy 300. výročia, kedy sa oslavy uskutočnili pred farským kostolom a&nbsp;na kalvárii. Svätú omšu mal prísť celebrovať biskup Feranec, avšak štátna moc to zamietla. Svätá omša bola napokon bez biskupa a&nbsp;len v&nbsp;Kostole sv. Františka z&nbsp;Assisi.
                </p>

                <p>
                    Za jeho pôsobenia sa vykonala generálna oprava interiéru kostola, stavba novej fary, rozbehla sa stavba kostola v&nbsp;Kriváni a&nbsp;príprava na začatie stavby kostola v&nbsp;Korytárkach. Biskupský úrad na žiadosť Závodského dovolil po roku 1989 slúžiť sväté omše v&nbsp;Dome smútku v&nbsp;Kriváni, ktoré tam boli totalitnou mocou zakázané od roku 1972.
                </p>

                <x-partials.picture titleSlug="zav-020" side="right" dimensionSource="medium" columns="2"/>

                <p>
                    Na veľkonočnú vigíliu Veľkej noci a&nbsp;na sviatok Božieho tela sa za priaznivého počasia začali znovu usporadúvať veľké eucharistické procesie mestom. Pastoračná činnosť sa rozšírila o&nbsp;Kriváň, Korytárky a&nbsp;Stožok, kde sa pravidelne v&nbsp;nedeľu začali sláviť sväté omše, na ktoré tamojší veriaci začali prichádzať v&nbsp;hojnejšom počte. Duchovné kvality, ale i&nbsp;podnikavého ducha v&nbsp;budovaní hmotných objektov farnosti si u&nbsp;svojho dekana veriaci veľmi vážili. V&nbsp;srdciach ľudí ostal zapísaný ako dobrý duchovný otec, prísny i&nbsp;láskavý zároveň.
                </p>

                <p>
                    Jozef Závodský zomrel náhle, v&nbsp;zápale práce, počas svojej pracovnej cesty do Prievidze. 17. februára 1993 mu na čerpacej stanici v&nbsp;Handlovej prišlo nevoľno a&nbsp;azda len Božím riadením ho tam našiel kňaz Jozef Kaniansky. Ten mu stihol udeliť posledné sviatosti a&nbsp;v jeho prítomnosti zomrel vo veku 66 rokov, v&nbsp;24. roku kňazstva. Pohrebné obrady vykonal v&nbsp;Detve pomocný biskup Peter Dubovský a&nbsp;následne boli jeho telesné ostatky prevezené na cintorín do rodných Sučian, kde ho biskup Dubovský pochoval 20. februára 1993.
                </p>
