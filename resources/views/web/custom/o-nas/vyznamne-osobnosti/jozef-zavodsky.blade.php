                <p>
                    Po úspešnom ukončení formácie v&nbsp;centrálnom Kňazskom seminári sv. Cyrila a&nbsp;Metoda v&nbsp;Bratislave bol biskupom Róbertom Pobožným 22. júna 1969 vo veku 42 rokov vysvätený za kňaza. Primičnú svätú omšu slávil v&nbsp;rodných Sučanoch 29. júna 1969. Na svoje prvé kaplánske pôsobisko bol poslaný do Bojníc. V&nbsp;roku 1970 bol poverený dočasnou výpomocou vo farnostiach Turany, Vrútky a&nbsp;Sučany, počas práceneschopnosti ich duchovných správcov. V&nbsp;tom istom roku bol následne poverený správou farnosti Vrícko, so zachovaním určenia výpomoci v&nbsp;Sučanoch. Od roku 1972 mu bolo pridelené kaplánske miesto vo Vrútkach, avšak s&nbsp;určením dočasného zastupovania správy jeho rodnej farnosti v&nbsp;Sučanoch, z&nbsp;dôvodu dlhotrvajúcich zdravotných ťažkostí Jozefa Horváta. Od roku 1974 ďalej pôsobil ako kaplán vo Vrútkach, kde bol o&nbsp;rok neskôr po Matejovi Zaťkovi vymenovaný za správcu tejto farnosti. K&nbsp;50. narodeninám ho v&nbsp;roku 1977 biskup Jozef Feranec poctil titulom honorárny dekan. Vyzdvihol jeho príkladný kňazský život, naplnený priateľskými vzťahmi s&nbsp;kňazmi ako aj s&nbsp;veriacimi. V&nbsp;pastorácii nikdy nešetril námahou a&nbsp;vždy sa staral o&nbsp;krásu Božích chrámov i&nbsp;ďalších cirkevných budov.
                </p>
