                <p>
                    Od 1. decembra 1980 bol poverený správou farnosti Detva. Po príchode do Detvy sa Závodský usiloval o&nbsp;zveľadenie duchovnej i&nbsp;hmotnej stránky farnosti. Pre zastrašovanie komunistami sa v&nbsp;80. rokoch 20. storočia malo odvahu v&nbsp;Detve prihlásiť na vyučovanie náboženskej výchovy v&nbsp;základných školách len menšia časť žiakov. Detvianski kňazi boli v&nbsp;roku 1982 udaní, že vyučujú deti náboženstvo v&nbsp;kostole a&nbsp;to v&nbsp;stredu a&nbsp;v piatok. Dekan Závodský poslal na odbor kultúry Okresného národného výboru, oddeleniu pre veci cirkevné list, v&nbsp;ktorom celú záležitosť veľmi diplomaticky vysvetlil a&nbsp;zažehnal.
                </p>

                <p>
                    Azda najvýznamnejšou mierou sa zapísal do dejín farnosti tým, že v&nbsp;rokoch 1982 - 1984 nechal postaviť novú farskú budovu. Tajný jezuitský biskup Peter Dubovský mu poradil, aby využil svoje osobné vojnové zásluhy a&nbsp;vyznamenanie pri vybavovaní povolenia stavby novej farskej budovy, keďže mu komunistické vedenie neustále kládlo prekážky.
                </p>
            </x-web.page.text-segment>
