                <h6 class="text-church-template">† <span class="ps-1 pe-2">8. 6. 1919</span> Detva</h6>

                <x-partials.picture titleSlug="pro-014" side="right" dimensionSource="medium" columns="3"/>

                <p>
                    Anton Prokop sa narodil 20. júna 1894, ako syn Emila a&nbsp;Júlie, rod. Hermanovej. Po skončení ľudovej školy v&nbsp;Detve, študoval na gymnáziu v&nbsp;Banskej Bystrici a&nbsp;po štvrtej triede gymnázia pokračoval na Obchodnej akadémii. Po jej ukončení sa zamestnal ako úradník v&nbsp;Hospodárskej banke v&nbsp;Trnave. Po čase prišiel k&nbsp;názoru, že svetský život nie je pre neho a&nbsp;pomýšľal vstúpiť do Spoločnosti Ježišovej - k&nbsp;jezuitom. Kvôli vojnovým nepokojom nebolo dovolené mladých mužov prijímať do kláštora. Žil zbožne a&nbsp;kláštorným spôsobom života sa usiloval žiť aspoň doma. V&nbsp;roku 1915 bol odvedený za vojaka, narukoval k&nbsp;pešiemu pluku v&nbsp;Lučenci, avšak pre krátkozrakosť nebol na bojovom fronte, pracoval ako úradník na železničných staniciach. Po skončení prvej svetovej vojny sa v&nbsp;roku 1918 vrátil domov do Detvy, kde pracoval ako kancelársky pomocník na notárskom úrade obce Detva.
                </p>

            </x-web.page.text-segment>
            <x-web.page.text-segment animation="fromleft">

                <p>
                    V&nbsp;roku 1919 vstúpilo na územie Československej republiky vojsko maďarských boľševikov. Pod heslom šírenia proletárskej revolúcie sa snažili nastoliť socialistický režim podľa sovietskeho vzoru. V&nbsp;pozadí tejto vojenskej akcie bol posledný zúfalý pokus podmaniť si Slovensko a&nbsp;zachovať integritu Uhorska. Vojsko sa dostalo do okolia Zvolena a&nbsp;boľševická horda Bélu Kuna vnikla aj do Detvy. Obyvatelia si ukrývali kone, vozy a&nbsp;iný majetok do stodôl do slamy, aby im to vojsko nezrekvirovalo.
                </p>

                <x-partials.picture titleSlug="pro-003" side="right" dimensionSource="medium" columns="5"/>

                <p>
                    Anton bol horlivým vlastencom. Asi vo štvrtok pred Turícami usporiadal spolu s&nbsp;kaplánom Alexandrom Vajcíkom pred kostolom schôdzu a&nbsp;vyzýval ľudí, aby sa vlastnými zbraňami, kosami a&nbsp;cepmi, vzopreli Maďarom. Na ukončenie zhromaždenia nechal Anton zaspievať slovenskú hymnu „Hej, Slováci.“ Trúsili sa reči, že za toto sa im budú boľševici chcieť pomstiť. a&nbsp;tak im obom v&nbsp;piatok odporúčal vedúci notár Alexander Engelman a&nbsp;farár Anton Kúdelka, aby čím skôr odišli. Kaplán Vajcík ušiel na koni do hôr a&nbsp;Anton hoci sľúbil, že odíde, neurobil tak.
                </p>

                <p>
                    Prokopova reč sa nepáčila promaďarsky zmýšľajúcim občanom, najmä Židom. Jeden z&nbsp;nich, Markus Löwy z&nbsp;Detvy, udal Prokopa ako pansláva a&nbsp;československého agitátora. Anton bol asertívny mladík, nebál sa nepriateľa, jeho reč bola odvážna a&nbsp;za pravdu sa nebál položiť i&nbsp;vlastný život. Keď ho zradca Markus navštívil, hovoril mu: <em>„Vážim si každého poriadneho človeka. Nie som antisemitista, ale mnohí židia sú hmotári, chamtiví po bohatstve, kšefty sú ich modlou, i&nbsp;v&nbsp;synagóge myslia na obchod. Za nič nemáte našu slovenskú kultúru, prispôsobujete sa Maďarom, nie Slovákom, hoci už dlhé roky žijete z&nbsp;našich mozoľov. Lenže vám je i&nbsp;národné prispôsobenie iba kšeftom. Prispôsobíte sa tej národnosti, ktorá vládne a&nbsp;je politicky silnejšia. Za Bacha ste nemčili, za Tizsu ste maďarčili, za Kúna boľševičíte, za Čechov budete češtiť, len k&nbsp;nám Slovákom nemáte úctu. Aj my máme právo na svoj život ako hociktorý iný národ, preto už raz musí prestať doterajšie maďarónske zúrenie proti slovenčine.“
                </p>

                <p>
                    </em>Po tomto stretnutí ho šiel Markus udať boľševickým pohlavárom. Tí ho 8. júna na Turíčnu nedeľu včasráno prišli zatknúť. Odviedli ho do Vagačovho domu, zavreli ho do pivnice a&nbsp;potom vyšetrovali. Jeho matka mohla byť prítomná. Obžalobcom bol Markus Löwy a&nbsp;obžaloba znela: <em>„Búril Detvanov proti vám, štval aj proti Židom, je nebezpečný antisemitista.“</em> Bol vynesený rozsudok smrti obesením: <em>„Vyvesiť tohto rebela na vhodné miesto, aby Detvanci, až prídu z&nbsp;lazov do kostola videli, že s&nbsp;nepriateľmi nežartujeme.“</em>
                </p>

            </x-web.page.text-segment>
