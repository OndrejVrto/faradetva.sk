        <x-web.page.subsection>
            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="bud-013" side="left" dimensionSource="medium" columns="5"/>

                <p>
                    Po piatich rokoch svojho účinkovania v&nbsp;pastorácii ako kaplán sa stal správcom novozriadenej farnosti v&nbsp;Hriňovej, o&nbsp;ktorej vznik sa sám pričinil. V&nbsp;Hriňovej najskôr začínal v&nbsp;roku 1927 ako „capelanus expositus“, kaplán v&nbsp;osobitnej expozitúre Hriňová, čiže samostatného územného celku, vyčleneného z&nbsp;farnosti Detva. Poldruha roka býval na viacerých miestach v&nbsp;súkromných domoch, až kým sa v&nbsp;júli 1928 nepodarilo postaviť novú faru a&nbsp;stal sa správcom novozriadenej farnosti.
                </p>

                <p>
                    V&nbsp;Hriňovej nepobudol dlho. Jeho nadanie a&nbsp;nadobudnuté skúsenosti bolo treba zužitkovať inde. V&nbsp;ukrutnej zime na prelome rokov 1928 - 1929 vážne ochorel a&nbsp;v&nbsp;obave, že pre nedostatok kňazov nebude mať pri sebe kaplána, požiadal biskupa Mariána Blahu o&nbsp;preloženie na miesto, kde by to jeho ochabnuté pľúca vydržali. K&nbsp;1. marcu 1929 dostal dispozíciu na uprázdnenú faru v&nbsp;Radvani pri Banskej Bystrici.
                </p>

            </x-web.page.text-segment>
            <x-web.page.text-segment animation="fromleft">

                <p>
                    Jedným z&nbsp;neposledných dôvodov odchodu Jozefa Búdu z&nbsp;Hriňovej bola aj možnosť ďalšieho postgraduálneho štúdia. Radvaň v&nbsp;tom čase mala 900 farníkov v&nbsp;mestečku a&nbsp;ešte približne 700 rímskokatolíkov v&nbsp;ôsmich zväčša protestantských filiálkach bez kostolov. Naproti tomu v&nbsp;rozsiahlej expozitúre Hriňová žilo 5215 farníkov a&nbsp;už vtedy bolo nevyhnutné, aby tretia detvianska kaplánska stanica bola disponovaná do Hriňovej.
                </p>

                <x-partials.picture titleSlug="bud-002" side="right" dimensionSource="medium" columns="6"/>

                <p>
                    Od 1. septembra 1930 bol Búda vymenovaný za gymnaziálneho profesora na Biskupskom gymnáziu v&nbsp;Banskej Bystrici, pri ktorom v&nbsp;tom čase existoval aj malý seminár. V&nbsp;tých rokoch zložil predpísané rigorózne skúšky na Teologickej fakulte Karlovej univerzity v&nbsp;Prahe a&nbsp;po úspešnej obhajobe dizertačnej práce <em>Dejiny miešaných manželstiev na Slovensku</em>, bol dňa 17. marca 1933 v&nbsp;pražskom Karolíne slávnostne promovaný za doktora posvätnej teológie. Biskup Blaha ho následne vymenoval za suplenta biblického štúdia na Vysokej škole teologickej v&nbsp;Kňazskom seminári sv. Karola Boromejského v&nbsp;Banskej Bystrici.
                </p>

                <p>
                    1. septembra 1934 Jozef Búda abdikoval zo svojho úradu farára v&nbsp;Radvani a&nbsp;bol vymenovaný za riadneho profesora biblika. Opustil farnosť a&nbsp;presťahoval sa do profesorského bytu v&nbsp;kňazskom seminári. Naďalej vyučoval aj na gymnáziu v&nbsp;Banskej Bystrici. Od roku 1936 bol diecéznym cenzorom a&nbsp;od roku 1938 prosynodálnym sudcom. V&nbsp;roku 1936 vznikla Cyrilometodská bohoslovecká fakulta Univerzity Komenského v&nbsp;Bratislave. Dr. Búda sa v&nbsp;Bratislave stal suplentom pre biblické jazyky - gréčtinu a&nbsp;hebrejčinu. Súbežne prednášal ešte aj v&nbsp;banskobystrickom seminári, ale v&nbsp;roku 1937 sa už presťahoval do Bratislavy a&nbsp;od roku 1938 prednášal biblické jazyky ako docent.
                </p>

