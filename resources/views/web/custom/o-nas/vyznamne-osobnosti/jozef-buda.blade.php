                <h6 class="text-church-template">* <span class="ps-1 pe-2">14. 9. 1898 </span>Nemecká (okr. Brezno)</h6>
                <h6 class="text-church-template">† <span class="ps-1 pe-2">23. 6. 1994 </span>Báč (okr. Dunajská Streda)</h6>

                <x-partials.picture titleSlug="bud-001" side="right" dimensionSource="medium" columns="2"/>

                <p>
                    Jozef Búda sa narodil v&nbsp;horehronskej dedine Nemecká 14. septembra 1898. Základnú školu navštevoval v&nbsp;rodnej obci, ďalej pokračoval na gymnáziu v&nbsp;Banskej Bystrici, ktoré ukončil maturitou v&nbsp;roku 1916. Teologické štúdia začal v&nbsp;Banskej Bystrici a&nbsp;pokračoval v&nbsp;Olomouci a&nbsp;Prahe, kde bol napokon 9. júla 1922 vysvätený za kňaza. Ako kaplán začal pôsobiť v&nbsp;Detvianskej Hute, v&nbsp;roku 1923 bol kaplánom v&nbsp;Detve, ale v&nbsp;tom istom roku sa stal chórovým kaplánom Sídelnej kapituly v&nbsp;Banskej Bystrici. Od roku 1924 plnil úlohu notára diecéznej kúrie vo Svätom Kríži nad Hronom, kým nebol o&nbsp;rok neskôr preložený za kaplána do Krupiny a&nbsp;v&nbsp;roku 1926 sa opäť stal kaplánom v&nbsp;Detve.
                </p>

                <p>
                    Vo všetkých týchto farnostiach, kde bol Búda kaplánom, bolo jeho náplňou pastorácie hlavne vyučovanie náboženstva v&nbsp;lazníckych školách. V&nbsp;každej farnosti ich bolo desať aj viac. Náboženstvo sa vyučovalo v&nbsp;každej triede dve hodiny týždenne. V&nbsp;detvianskej farnosti či sa išlo na Blato alebo na Piešť II., do štvortriedky v&nbsp;Detve alebo na Snohy či Raticov vrch v&nbsp;Hriňovej, bolo treba šliapať peši dve hodiny alebo aj viac. V&nbsp;krátke zimné dni odchádzal kaplán z&nbsp;fary ráno za tmy a&nbsp;vracal sa taktiež za tmy. Učilo sa aj tridsať hodín týždenne. Na týchto odľahlých miestach jednotlivých farností sa Jozef Búda učil poznávať a&nbsp;vnikať do mentality detí, mládeže a&nbsp;rodín. Tam sa mu zjavoval jednoduchý, skromný, ale aj úprimný život hlavne podpolianskych ľudí, no aj ich vášne a&nbsp;návyky. To všetko vedel potom bohato zužitkovať vo svojej neskoršej pastoračnej činnosti gymnaziálneho a&nbsp;vysokoškolského profesora a&nbsp;vychovávateľa kňazského dorastu.
                </p>

