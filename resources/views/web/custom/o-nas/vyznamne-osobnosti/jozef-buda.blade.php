                <p>
                    V&nbsp;roku 1939 ho vymenovali za mimoriadneho a&nbsp;v&nbsp;roku 1942 za riadneho univerzitného profesora biblického štúdia Starého zákona. V&nbsp;rokoch 1942 - 1945 viedol kurz gréckeho jazyka a&nbsp;grécky seminár aj na Filozofickej fakulte Slovenskej univerzity. V&nbsp;40. rokoch 20. storočia bol ako univerzitný profesor ustanovený z&nbsp;poverenia Slovenského biskupského zboru za prvého kazateľa pre vysokoškolskú mládež v&nbsp;jezuitskom kostole v&nbsp;Bratislave.
                </p>

            </x-web.page.text-segment>
            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="bud-006" side="left" dimensionSource="medium" columns="3"/>

                <p>
                    Po druhej svetovej vojne vznikol problém s&nbsp;obsadením úlohy rektora kňazského seminára v&nbsp;Banskej Bystrici, a&nbsp;preto v&nbsp;roku 1945 biskup Andrej Škrábik povolal z&nbsp;Bratislavy do tejto pozície profesora Búdu, ktorý v&nbsp;seminári zároveň prednášal biblikum a&nbsp;pastorálku. V&nbsp;pozícii rektora kňazského seminára bol iba jeden rok, pretože v&nbsp;roku 1946 sa stal dekanom teologickej fakulty v&nbsp;Bratislave a&nbsp;opäť sa z&nbsp;Banskej Bystrice presťahoval do svojho bytu v&nbsp;Svoradove. V&nbsp;roku 1947 slávil svoje strieborné kňazské jubileum a&nbsp;pri tejto príležitosti bol pápežom Piom XII. vymenovaný za pápežského komorníka (Monsignor). Následne v&nbsp;rokoch 1948 - 1950 zastával úlohu prodekana fakulty.
                </p>

                <p>
                    Jeho plodné vedecko-pedagogické pôsobenie bolo prerušené administratívnym zásahom komunistického režimu. Obdobie od 28. februára 1950, po domovej prehliadke a&nbsp;po vzatí do väzby, až do 18. apríla 1954 prežil v&nbsp;rozličných väzniciach po celom Československu, z&nbsp;toho vyše šesť mesiacov bez súdu. Vyšetrovaciu väzbu strávil v&nbsp;Bratislave, Mučeníkoch, Nových Zámkoch, Podolínci a&nbsp;v&nbsp;Prešove. Odsúdený bol 9. júna 1952 na stratu slobody na štyri roky, prepad celého majetku a&nbsp;stratu čestných občianskych práv, pretože neschvaľoval proces s&nbsp;tzv. vlastizradnými biskupmi a&nbsp;neoznámil úmysel Dr. Mitošinku a&nbsp;spoločníkov utiecť za hranice. Vo väzení bol v&nbsp;Leopoldove, Ilave a&nbsp;na Mírove. Po návrate z&nbsp;väzenia sa mohol zamestnať len civilne, nemohol účinkovať ako kňaz a&nbsp;profesor, lebo nedostal štátny súhlas. Do roku 1961 pracoval v&nbsp;Laboratórnych potrebách v&nbsp;Bratislave a&nbsp;potom ako brigádnik v&nbsp;Zberných surovinách na Miletičovej ulici. Od roku 1963 účinkoval ako duchovný správca rehoľných sestier v&nbsp;českom Zvíkovci.
                </p>

            </x-web.page.text-segment>
            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="bud-008" side="right" dimensionSource="medium" columns="4"/>

                <p>
                    Na bohosloveckú fakultu v&nbsp;Bratislave sa vrátil až za Dubčekovej éry, od 1. októbra 1968 znovu pôsobil ako profesor Starého zákona. V&nbsp;ťažkých rokoch normalizácie bol veľkou morálnou oporou mnohým seminaristom, mladým kňazom a&nbsp;rehoľným sestrám, ktoré museli žiť svoje zasvätenie v&nbsp;ilegalite. Popri pôsobení na bohosloveckých fakultách ako odborník na Starý zákon, prispieval do periodík Katolícke noviny, Duchovný pastier, Kultúra, Nová práca. Svoje štúdie uverejňoval v&nbsp;zborníkoch Catholica Theologica Slovaca a&nbsp;Biblica.
                </p>

                <p>
                    V&nbsp;roku 1973 ho biskup Jozef Feranec vymenoval za banskobystrického honorárneho kanonika. Mnohí ho charakterizovali ako veľmi skromného kňaza, ktorý bol tichý, jednoduchý, nenáročný a&nbsp;asketický. Nemal rád dlhé reči, ale vedel byť úctivý, žartovný a&nbsp;taktný. Bol mimoriadnym ctiteľom Božského Srdca Ježišovho a&nbsp;Panny Márie, ktorú často tituloval „Panenská Matka.“
                </p>

                <x-partials.picture titleSlug="bud-010" side="left" dimensionSource="medium" columns="3"/>

                <p>
                    V&nbsp;roku 1976 skončil svoju vedecko-pedagogickú činnosť profesora a&nbsp;odišiel do dôchodku. Od 1. októbra 1978 bol duchovným v&nbsp;Charitnom domove v&nbsp;Báči, kde dožil v&nbsp;opatere rehoľných sestier Inštitútu preblahoslavenej Panny Márie, ľudovo nazývané ako anglické panny (Congregatio Jesu).
                </p>

                <p>
                    V&nbsp;roku 1992 na 70. výročie kňazskej vysviacky mu pápež Ján Pavol II. udelil za celoživotný kňazský vzor a&nbsp;charakter, titul pápežského preláta. Zomrel v&nbsp;Báči 23. júna 1994 v&nbsp;96. roku života a&nbsp;72. roku kňazstva. 27. júna 1994 ho v&nbsp;Báči pochoval biskup Rudolf Baláž. Pohrebnú svätú omšu s&nbsp;ním koncelebrovali štyria biskupi a&nbsp;približne 60 kňazov.
                </p>

