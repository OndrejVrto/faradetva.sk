                <p>
                    V&nbsp;Detve si Štrbáň pripomínal v&nbsp;roku 1926 strieborné (25 rokov) a&nbsp;v&nbsp;roku 1951 aj zlaté (50 rokov) jubileum kňazskej vysviacky. Ako bývalý člen františkánskej rehole obnovil v&nbsp;Detve laický Tretí rád sv. Františka. Počas jeho pôsobenia boli v&nbsp;detvianskej farnosti zakúpené nové zvony do farského kostola, elektrifikovaný kostol a&nbsp;bol postavený nový chudobinec. Štrbáň sa pravidelne zúčastňoval ľudových misií, počas ktorých ho vo farnosti zastupovali jeho kapláni.
                </p>

                <x-partials.picture titleSlug="str-015" side="left" dimensionSource="medium" columns="2"/>

                <p>
                    Mnohopočetné žiadosti pre odchod z&nbsp;náročnej správy detvianskej farnosti, do penzie zo zdravotných dôvodov a&nbsp;neskôr kvôli veku, boli biskupmi odmietané aj kvôli neústupčivosti a&nbsp;naliehaní farníkov, ktorí si svojho vzácneho dušpastiera nechceli pustiť za žiadnych okolností. Jeho stálou túžbou bolo zomrieť ako rehoľník, okrem iných spomínaných reholí, zvažoval aj vstup k&nbsp;školským bratom či lazaristom.
                </p>

                <p>
                    Po nástupe komunistami dosadeného Jána Decheta za kapitulného vikára v&nbsp;roku 1950 a&nbsp;zmene pomerov v&nbsp;diecéze, sa najskôr v&nbsp;roku 1951 zriekol dekanského úradu a&nbsp;v&nbsp;závere roka 1952 mu bol Krajským národným výborom v&nbsp;Banskej Bystrici odňatý štátny súhlas vo verejnej duchovnej správe. Totalitný Slovenský úrad pre veci cirkevné ho v&nbsp;spolupráci s&nbsp;kapitulným vikárom násilne preložil za správcu fary do Bacúrova od roku 1953. Hoci k&nbsp;1. januáru 1959 odišiel do dôchodku, bol Dechetom nútený zostať bývať v&nbsp;Bacúrove aj naďalej, dokonca bez poberania platu alebo akejkoľvek odmeny. Keď však cítil, že veľmi slabne, prišiel v&nbsp;roku 1960 dožiť do svojho dlhoročného pôsobiska na Podpoľaní, kde aj 11. decembra 1960 ako 82-ročný zomrel v&nbsp;60. roku kňazstva. Pochovaný bol arcidekanom Jozefom Hudecom 13. decembra 1960 za účasti 40 kňazov, na detvianskom cintoríne, pri druhom zastavení krížovej cesty.
                </p>

