                <p>
                    Ján Štrbáň bol za nového detvianskeho farára vymenovaný v&nbsp;októbri 1921. Pamätníkmi bol opísaný ako muž výzorom nevysoký, usmievavý, v&nbsp;spoločnosti skromný, pokorný, plný horlivosti za spásu duší. Oravská, Bohu oddaná duša, s&nbsp;horlivou túžbou po svätosti. Veľmi mnoho spovedal. V&nbsp;dlhých rozjímaniach a&nbsp;modlitbách  trávil aj noci. To vyžarovalo aj v&nbsp;jeho nadšenom kázaní. Angažoval sa v&nbsp;sociálnej i&nbsp;kultúrnej oblasti. Prispieval do cirkevných časopisov <em>Náš priateľ</em>, <em>Kráľovná svätého ruženca</em> a&nbsp;<em>Svätá rodina</em>. Zostavil tri modlitebné knižky: <em>Modlitby a&nbsp;piesne</em> (1924); <em>Môj poklad</em> (1929); <em>Ježiš, náš tešiteľ. Modlitebná knižočka pre starších ľudí a&nbsp;najlepší priateľ v&nbsp;staršom veku</em> (1933). V&nbsp;roku 1934 bol pápežom Piom XI. ocenený titulom pápežský komorník (Monsignor). V&nbsp;Detve pôsobil 32 rokov ako farár a&nbsp;od roku 1937 aj ako dekan zvolenského dekanátu. Svoje rozhodnutie odísť od františkánov síce viackrát oľutoval, pokúšal sa dokonca opakovane odísť z&nbsp;Detvy k&nbsp;redemptoristom, tešiteľom, i&nbsp;jezuitom, jeho pastoračnú horlivosť vo farnosti to však neumenšilo.
                </p>

                <p>
                    Rok 1938 prežila Detva oslavami 300-ročnej pamiatky založenia mesta. Slávnostnú svätú omšu celebroval 17. júla na námestí pred kostolom biskup Marián Blaha. Kázeň predniesol detviansky dekan, predseda jubilejného výboru, Ján Štrbáň. Zachoval sa z&nbsp;nej ohlas v&nbsp;dobovej tlači: <em>„...vari by sa aj bil za svojich farníkov, tak ich ľúbi. Je máločo dojímavejšie ako to, ako lipnú k&nbsp;sebe Detvania a&nbsp;ich duchovný pastier. Svedectvo, ktoré vystavil o&nbsp;nábožnom, dobrom ľude detvianskom, je veľmi krásna a&nbsp;zriedkavá vec. Krásna vec, keď kňaz môže zavolať do sveta: Na svoj ľud som hrdý!“</em>. Hlavným bodom slávností bola popoludní národopisná slávnosť na kalvárii. Zúčastnilo sa jej asi desaťtisíc ľudí, ktorí mali možnosť sledovať zvyky detvianskeho ľudu od kolísky až po hrob.
                </p>

