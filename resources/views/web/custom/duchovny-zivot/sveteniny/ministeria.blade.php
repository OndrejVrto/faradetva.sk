        <x-web.page.subsection>
            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="min-001" side="right" dimensionSource="medium" columns="5" />

                <p>
                    Niektoré služby určené pre náležité slávenie bohoslužby a&nbsp;pre pomoc Božiemu ľudu v&nbsp;jeho potrebách ustanovila Cirkev už v&nbsp;najstarších dobách. Tieto služby, ktoré sa zverovali veriacim, aby ich vykonávali pri liturgii a&nbsp;v&nbsp;charitatívnej činnosti, sa prispôsobovali rozličným okolnostiam. Tieto úlohy sa spravidla udeľovali osobitným obradom. Veriaci tak získal Božie požehnanie a&nbsp;bol zaradený do vyčlenenej skupiny, čiže mal osobitné postavenie, aby mohol vykonávať nejaký cirkevný úrad.
                </p>
                <p>
                    Niektoré z&nbsp;týchto úradov, užšie spojené s&nbsp;liturgickou činnosťou, sa postupne stali službami, ktoré predchádzali prijatiu sviatostného svätenia. V&nbsp;latinskej cirkvi to boli ostiariát, lektorát, exorcitát a&nbsp;akolytát a&nbsp;nazývali sa nižšími sväteniami - vzhľadom k&nbsp;subdiakonátu, diakonátu a&nbsp;kňazstvu, ktoré boli označované ako vyššie svätenia. Tieto nižšie svätenia boli všeobecne - aj keď nie všade - vyhradzované tým, ktorí cez ne smerovali ku kňazstvu.
                </p>
                <p>
                    Duch Pána Ježiša, trvalý zdroj života a&nbsp;poslania Cirkvi, rozdeľuje členom Božieho ľudu dary, ktoré každému z&nbsp;nich umožňujú rôznym spôsobom prispievať k&nbsp;budovaniu Cirkvi a&nbsp;hlásaniu evanjelia. Tieto charizmy, nazývané tiež ministériá, keďže sú verejne uznané a&nbsp;ustanovené Cirkvou, sú dané k&nbsp;dispozícii spoločenstvu a&nbsp;jeho misii v&nbsp;trvalej forme.
                </p>
                <p>
                    V&nbsp;niektorých prípadoch má takýto príspevok vo forme služby svoj pôvod v&nbsp;konkrétnej sviatosti, vo sviatosti posvätného stavu. Ďalšie úlohy boli ustanovené v&nbsp;Cirkvi v&nbsp;priebehu dejín a&nbsp;zverené jednotlivým veriacim prostredníctvom nesviatostného liturgického obradu, na základe krstného kňazstva ako osobitná forma jeho vykonávania, ktorá má pomôcť biskupom, kňazom a&nbsp;diakonom v&nbsp;ich špecifickej službe.
                </p>

                <x-partials.picture titleSlug="min-009" side="left" dimensionSource="medium" columns="6" />

                <p>
                    Pretože sa nižšie svätenia v&nbsp;priebehu stáročí menili a&nbsp;mnohé úlohy im vyhradené v&nbsp;skutočnosti vykonávali laici, pápež sv. Pavol VI. v&nbsp;roku 1972 zrušil nižšie svätenia a&nbsp;ponechal len dve ministériá: lektorát a&nbsp;akolytát. Hoci tieto služby boli udeľované aj iným spôsobilým veriacim mužom, považovali sa za predprípravu k&nbsp;neskoršiemu prijatiu sviatosti vysviacky, preto boli vyhradené len osobám mužského pohlavia. Základom týchto služieb ustanovených Cirkvou je však spoločný stav tých, čo boli pokrstení a&nbsp;vo sviatosti krstu prijali kráľovské kňazstvo, preto sa tieto služby zásadne líšia od služby prijatej vo sviatosti vysviacky.
                </p>
                <p>
                    Keďže sú ministériá ako laické služby založené na sviatosti krstu, môžu byť zverené všetkým veriacim, ktorí sú pokladaní za spôsobilých, či už sú mužského alebo ženského pohlavia. Pápež František v&nbsp;roku 2021 umožnil, aby laici, ako muži, tak i&nbsp;ženy, ktorí majú vek a&nbsp;vlohy stanovené dekrétom konferencie biskupov, mohli byť predpísaným liturgickým obradom natrvalo prijatí do služby lektora a&nbsp;akolytu. Toto udelenie služieb im však nedáva právo na poskytovanie hmotného zabezpečenia alebo mzdy zo strany Cirkvi. V&nbsp;tom istom roku pápež František zriadil laické ministérium katechétu. Službe, ktorá existuje v&nbsp;Cirkvi v&nbsp;rôznych podobách od prvopočiatkov, sa tým priznáva osobitná dôležitosť v&nbsp;duchu Druhého vatikánskeho koncilu, ktorý vyzdvihol aktívnu úlohu laikov, mužov i&nbsp;žien.
                </p>
            </x-web.page.text-segment>
