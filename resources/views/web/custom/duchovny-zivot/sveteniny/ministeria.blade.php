    <x-web.page.section name="PAGE: ({{ $pageData->title }}) PART II -" class="static-page pad_t_50 pad_b_50">
        <x-web.page.subsection title="Lektorát">

            <x-web.page.text-segment>

                <x-partials.picture titleSlug="min-007" side="right" dimensionSource="medium" columns="4" />
                <p>
                    V&nbsp;súčasnej liturgii má dôležitú úlohu taktiež lektor, ktorý číta čítania zo Starého zákona a&nbsp;z&nbsp;apoštolských listov. Lektori majú podiel na hlásaní Božieho slova, ktorému zhromaždený ľud naslúcha. Lektor je členom liturgického zhromaždenia, a&nbsp;preto by po celú liturgiu mal byť prítomný v&nbsp;lodi kostola, odtiaľ potom pristupuje k&nbsp;ambone - k&nbsp;miestu, ktoré je vyhradené len k&nbsp;prednesu Božieho slova.
                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">
                    Akolytát
                </h3>

                <p>
                    Akolyta je poverený k&nbsp;liturgickej službe pri oltári: pripravuje oltár, posluhuje kňazovi alebo diakonovi, rozdáva sväté prijímanie veriacim. K&nbsp;adorácii môže vystaviť Eucharistiu v&nbsp;cibóriu, viesť adoráciu a&nbsp;odložiť Eucharistiu, nemôže s&nbsp;ňou však žehnať. Mimo služby pri Pánovom oltári nosili akolyti taktiež svetlo pri slávení liturgie. Akolyti môžu po svätej omši zanášať sväté prijímanie chorým a&nbsp;nevládnym do ich príbytkov.
                </p>
            </x-web.page.text-segment>
        </x-web.page.subsection>

        <x-web.page.subsection title="Katechista">

            <x-web.page.text-segment>

                <x-partials.picture titleSlug="min-004" side="left" dimensionSource="full" columns="3" />

                <p>
                    Služba katechétu je v&nbsp;Cirkvi veľmi starobylá. Už od začiatku svojej existencie kresťanské spoločenstvo poznalo všeobecnú formu služby, ktorú konkrétne uskutočňovali muži a&nbsp;ženy, ktorí v&nbsp;poslušnosti Duchu Svätému zasvätili svoj život budovaniu Cirkvi. Celé dejiny evanjelizácie v&nbsp;uplynulých dvetisíc rokoch jasne ukazujú, aká účinná bola misia katechétov. Bez akéhokoľvek umenšenia vlastného poslania biskupa, ktorý je vo svojej diecéze - spoločne s&nbsp;presbytériom, s&nbsp;ktorým sa podieľa na tej istej pastoračnej starostlivosti - prvým katechétom, alebo znižovania osobitnej zodpovednosti rodičov za kresťanskú výchovu ich detí, je nevyhnutné oceniť prítomnosť laikov a&nbsp;laičiek, ktorí sa cítia byť na základe vlastného krstu povolaní spolupracovať na diele katechézy.
                </p>

                <x-partials.picture titleSlug="min-005" side="right" dimensionSource="full" columns="3" />

                <p>
                    Osobitná funkcia, ktorú katechéta plní, sa však konkretizuje v&nbsp;rámci iných služieb v&nbsp;kresťanskom spoločenstve. Katechéta je totiž povolaný prejaviť svoju kompetenciu v&nbsp;prvom rade v&nbsp;pastoračnej službe odovzdávania viery, ktorá sa rozvíja v&nbsp;rôznych etapách: od prvého ohlasovania, ktoré vovádza do kerygmy, cez vyučovanie, ktoré vedie k&nbsp;uvedomeniu si nového života v&nbsp;Kristovi a&nbsp;zvlášť pripravuje na sviatosti kresťanskej iniciácie, až po trvalú formáciu. Katechéta je zároveň svedkom viery, učiteľom i&nbsp;mystagógom, sprevádzajúcim i&nbsp;pedagógom, ktorý vyučuje v&nbsp;mene Cirkvi.
                </p>

                <p>
                    Nie všetci, ktorí sa stali katechétmi a&nbsp;katechétkami (napr. učitelia náboženstva), môžu pristúpiť k&nbsp;ministériu katechétu. Toto ministérium je vyhradené tým, ktorí zodpovedajú požiadavkám podľa uváženia diecézneho biskupa. Prvou požiadavkou zo všetkých je dimenzia povolania slúžiť Cirkvi tam, kde to biskup považuje za najkvalifikujúcejšie. Ministérium sa neudeľuje ako osobná odmena, ale ako služba miestnej cirkvi a&nbsp;tam, kde biskup považuje prítomnosť katechétu za nevyhnutnú. Ministérium katechétu sa opiera o&nbsp;duchovný život s&nbsp;rozmerom komunity a&nbsp;modlitby a&nbsp;je to služba, ku ktorej sa dospieva príslušnou solídnou formáciou.
                </p>
