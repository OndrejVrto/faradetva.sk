            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="pohr-006" side="right" dimensionSource="full" columns="4" />

                <p>
                    Aby sme mohli vstať s&nbsp;Kristom z&nbsp;mŕtvych, musíme zomrieť s&nbsp;Kristom, „vzdialiť sa z&nbsp;tela a&nbsp;bývať u&nbsp;Pána“ (2Kor 5,8). Inštrukciou Piam et constantem z&nbsp;5. júla 1963, Sväté ofícium ustanovilo, aby sa „verne zachovával zvyk pochovávať mŕtve telá veriacich“, pričom sa dodáva, že kremácia „neprotirečí kresťanskému náboženstvu“ a&nbsp;už sa nemajú odopierať sviatosti a&nbsp;pohrebné obrady tým, ktorí si želali byť spopolnení. Platí to však za podmienky, že ich želanie nie je motivované „popieraním kresťanských dogiem, alebo sektárskym zmýšľaním, alebo nenávisťou voči katolíckemu náboženstvu a&nbsp;Cirkvi“.
                </p>

                <x-partials.picture titleSlug="pohr-005" side="left" dimensionSource="full" columns="3" />

                <p>
                    Kristovo zmŕtvychvstanie, ktoré predstavuje vrcholnú pravdu kresťanskej viery, sa od počiatkov kresťanstva hlása ako podstatná súčasť veľkonočného tajomstva. Prostredníctvom svojej smrti a&nbsp;zmŕtvychvstania nás Kristus oslobodil od hriechu a&nbsp;umožnil nám vstúpiť do nového života: „... aby sme tak, ako bol Kristus vzkriesený z&nbsp;mŕtvych Otcovou slávou, aj my žili novým životom“ (Rim 6,4). Okrem toho je zmŕtvychvstalý Kristus počiatkom a&nbsp;zdrojom nášho budúceho života: „Kristus vstal z&nbsp;mŕtvych, prvotina zosnulých... ako všetci umierajú v&nbsp;Adamovi, tak zasa všetci ožijú v&nbsp;Kristovi.“ (1Kor 15,20-22).
                </p>

                <p>
                    Vďaka Kristovi má kresťanská smrť pozitívny zmysel. Liturgia Cirkvi sa modlí: „Veď tým, čo veria v&nbsp;teba, Bože, život sa neodníma iba mení; a&nbsp;keď skončíme život v&nbsp;smrteľnom tele, máme pripravený večný príbytok v&nbsp;nebesiach.“ Pri smrti sa duša oddelí od tela, no pri vzkriesení Boh dáva neporušiteľný život nášmu premenenému telu, ktoré spojí s&nbsp;našou dušou. Aj dnes je Cirkev povolaná ohlasovať vieru vo vzkriesenie: „Vzkriesenie mŕtvych je vierou kresťanov: stali sme sa nimi vierou v&nbsp;toto vzkriesenie.“
                </p>

                <p>
                    Nasledujúc najstaršiu kresťanskú tradíciu Cirkev neúnavne odporúča, aby boli telá zosnulých pochované na cintoríne alebo na inom posvätnom mieste. Pri pamiatke na Pánovu smrť, pochovanie do hrobu a&nbsp;vzkriesenie, teda tajomstvo, vo svetle ktorého sa vyjavuje kresťanský zmysel smrti, je pochovanie tela tou najvhodnejšou formou na vyjadrenie viery a&nbsp;nádeje vo vzkriesenie tela. Cirkev, ktorá ako matka sprevádza kresťana počas jeho pozemskej púte, v&nbsp;Kristovi obetuje Otcovi dieťa jeho milosti a&nbsp;s&nbsp;nádejou, že ho vzkriesi v&nbsp;sláve, ukladá jeho telesné pozostatky do zeme.
                </p>

