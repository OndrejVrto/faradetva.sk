                <p>
                    Kresťanská liturgia pohrebu je slávením Kristovho veľkonočného tajomstva, pri ktorom sa Cirkev modlí za svoje deti, začlenené sviatosťou krstu do smrti a&nbsp;zmŕtvychvstania Ježiša Krista. Preto Cirkev, ako súcitná matka, svojimi modlitbami a&nbsp;prinášaním obety svätej omše chce byť pre zomrelých duchovnou pomocou a&nbsp;pre pozostalých útechou i&nbsp;nádejou v&nbsp;ich žiali, že všetci pokrstení v&nbsp;Krista raz vstanú k&nbsp;novému životu.
                </p>

                <p>
                    Pri pohrebe svojich veriacich Cirkev s&nbsp;dôverou slávi veľkonočné tajomstvo, aby tí, čo boli krstom pričlenení k&nbsp;zomrelému a&nbsp;vzkriesenému Kristovi, smrťou vošli s&nbsp;ním do života. Ich duše sa majú očistiť a&nbsp;prejsť do spoločenstva svätých a&nbsp;vyvolených v&nbsp;nebi, ich telá očakávajú blaženú nádej Kristovho príchodu a&nbsp;vzkriesenie mŕtvych. Preto Cirkev prináša za zomrelých veľkonočnú eucharistickú Kristovu obetu a&nbsp;koná za nich prosebné modlitby. Chce zomrelým vyprosiť duchovnú pomoc a&nbsp;živým poskytnúť útechu nádeje. Veď všetky údy tajomného Kristovho tela tvoria jeho spoločenstvo.
                </p>

                <x-partials.picture titleSlug="pohr-004" side="left" dimensionSource="full" columns="4" />

                <p>
                    Pri pohrebe svojich bratov a&nbsp;sestier majú kresťania vyjadriť svoju pevnú nádej na večný život. Zároveň však majú dbať na zmýšľanie svojich súčasníkov a&nbsp;na smútočné zvyky svojej krajiny. Čo by však odporovalo evanjeliu, nech sa usilujú zmeniť, aby sa pri kresťanskom pohrebe prejavovala veľkonočná viera a&nbsp;skutočný duch evanjelia. Zaiste treba odmietnuť prázdnu, vonkajšiu honosnosť pohrebu, patrí sa však preukazovať úctu telám zomrelých veriacich, veď boli chrámom Ducha Svätého.
                </p>

                <p>
                    Prv než zomrelého uložia do hrobu, majú sa zhromaždiť aspoň príbuzní a&nbsp;-&nbsp;ak je možné - aj ostatní veriaci, aby si pri bohoslužbe slova vypočuli slová útechy, ktorú dáva viera, aby sa zúčastnili na eucharistickej obete a&nbsp;napokon, aby sa rozlúčili so zomrelým. Obrad posledného rozlúčenia so zomrelým sa nemá chápať ako nejaké očisťovanie zomrelého - to sa deje skôr prostredníctvom eucharistickej obety - ale má to byť posledné rozlúčenie, pri ktorom kresťanské spoločenstvo pozdravuje jedného zo svojich členov prv, než sa jeho telo odnesie a&nbsp;pochová.
                </p>

