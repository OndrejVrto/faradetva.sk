        <x-web.page.subsection title="Uctievanie svätej Matky Pána Ježiša" animation="fromleft">

            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="pob-017" side="right" dimensionSource="medium" columns="3" />

                <p>
                    Ľudová zbožnosť k&nbsp;Preblahoslavenej Panne Márii, rozličná vo svojich prejavoch a&nbsp;hlboká vo svojom zdôvodnení, je významná a&nbsp;všeobecná cirkevná skutočnosť. Vyplýva z&nbsp;viery a&nbsp;lásky Božieho ľudu ku Kristovi, Vykupiteľovi ľudského pokolenia a&nbsp;z&nbsp;pochopenia spásonosného poslania, ktoré Boh zveril nazaretskej Márii, pre ktoré svätá Panna je nielen Matkou Pána a&nbsp;Spasiteľa, ale v&nbsp;pláne milosti aj Matkou všetkých ľudí.
                </p>
                <p>
                    Medzi dňami venovanými Preblahoslavenej Panne Márii vystupuje do popredia sobota, ktorá dosiahla stupeň liturgickej spomienky Panny Márie. V&nbsp;niektorých oblastiach sa v&nbsp;13.&nbsp;deň každého mesiaca veriaci schádzajú - v&nbsp;spomienke na zjavenia Panny Márie vo Fatime v&nbsp;roku 1917 - k&nbsp;mariánskym modlitbám. V&nbsp;súvislosti so zjaveniami vo Fatime vznikla na základe veľkého prisľúbenia Panny Márie Pobožnosť piatich prvých sobôt, ktorá sa koná s&nbsp;úmyslom odčiniť urážky spôsobené Nepoškvrnenému Srdcu Panny Márie. Fatimské soboty sa v&nbsp;našej diecéze organizujú na mariánskom pútnickom mieste v&nbsp;Starých Horách. Prvú sobotu mesiaca býva v&nbsp;starohorskej bazilike alebo na Studničke pravidelný dopoludňajší program mariánskej úcty, ktorý je zakončený slávnostnou svätou omšou. Fatimské soboty bývajú rovnako organizované v&nbsp;popoludňajších hodinách aj na Raticovom vrchu v&nbsp;Hriňovej.
                </p>
                <x-partials.picture titleSlug="pob-018" side="left" dimensionSource="medium" columns="3" />
                <p>
                    Jednou z&nbsp;najvynikajúcejších modlitieb k&nbsp;Pánovej Matke je Ruženec. Modlitba posvätného ruženca je v&nbsp;podstate kontemplatívna, ktorá si vyžaduje pokojný rytmus a&nbsp;akoby predlžované zamýšľanie sa podporujúce toho, kto sa modlí, v&nbsp;rozjímaní o&nbsp;tajomstvách Pánovho života. Modlitba ruženca môže byť súkromná, ale zvlášť sa vyzdvihuje komunitný charakter tejto modlitby. V&nbsp;našej farnosti sa modlíme posvätný ruženec (päť desiatkov) každý deň pred začiatkom večernej svätej omše. Medzi ďalšie významné pobožnosti k&nbsp;Panne Márii radíme: Anjel Pána, Regina caeli (Raduj sa, nebies Kráľovná), Loretánske litánie, Zasvätenie - zverenie sa Panne Márii, Karmelitánsky škapuliar, Mariánske medaily, Hymnus Akatist.
                </p>
