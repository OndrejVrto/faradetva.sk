        <x-web.page.subsection title="Uctievanie svätých a&nbsp;blahoslavených">

            <x-web.page.text-segment>

                <x-partials.picture titleSlug="pob-019" side="right" dimensionSource="medium" columns="2" />

                <p>
                    Uctievanie svätých a&nbsp;blahoslavenýchKult svätých, predovšetkým mučeníkov, je veľmi dávnou cirkevnou skutočnosťou. Cirkevná náuka a&nbsp;jej liturgia predkladajú svätých a&nbsp;blahoslavených, ktorí už jasne kontemplujú trojjediného Boha, za príklad Božiemu ľudu. Svätí sú historickí svedkovia všeobecného povolania k&nbsp;svätosti, významní Pánovi učeníci, vzory evanjeliového života, obyvatelia neba, orodovníci a&nbsp;priatelia ešte putujúcich na zemi, patróni miestnych cirkví.
                </p>


                <p>
                    Konečným cieľom úcty k&nbsp;svätým je vždy Božia sláva a&nbsp;posvätenie človeka prostredníctvom života dokonale zladeného s&nbsp;Božou vôľou a&nbsp;nasledovanie čností tých, ktorí boli vynikajúcimi Pánovými učeníkmi. Osobitnú úctu v&nbsp;prejavoch zbožnosti majú aj anjeli, v&nbsp;ľudovej zbožnosti zvlášť úcta k&nbsp;anjelovi strážcovi.
                </p>
                <p>
                    Uctievanie svätých a&nbsp;blahoslavených sa prejavuje najmä v&nbsp;liturgických spomienkach, sviatkoch a&nbsp;slávnostiach, ale aj v&nbsp;rôznych pobožnostiach: v&nbsp;litániách k&nbsp;svätým, v&nbsp;úcte relikvií svätých, v&nbsp;uctievaní obrazov, sôch, reliéfov alebo iných stvárnení svätých či v&nbsp;procesiách.
                </p>
