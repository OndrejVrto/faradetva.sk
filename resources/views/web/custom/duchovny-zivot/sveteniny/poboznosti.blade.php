                <p>
                    V&nbsp;cezročnom období má po Nedeli Zoslania Ducha Svätého osobitné miesto Slávnosť Najsvätejšieho Kristovho tela a&nbsp;krvi. Typickými prvkami ľudovej zbožnosti počas slávnosti je výzdoba ulíc, kvety a&nbsp;oltáre, kde má byť pri zastaveniach procesie umiestnená Najsvätejšia sviatosť. Zvlášť rozšíreným prejavom eucharistického kultu je Adorácia Najsvätejšej sviatosti.
                </p>

                V&nbsp;adorácii Najsvätejšej sviatosti sa zbližujú liturgické formy a&nbsp;prejavy ľudovej zbožnosti, a&nbsp;to v&nbsp;spôsoboch:

                <ul class="ms-4 list-unstyled">
                    <li>a) jednoduchá návšteva Najsvätejšej sviatosti uloženej vo svätostánku kostola; </li>
                    <li>b) adorácia pred vyloženou Oltárnou sviatosťou v&nbsp;monštrancii alebo v&nbsp;cibóriu;</li>
                    <li>c) takzvaná „večná poklona“ alebo „celodenná poklona“, ktorej sa zúčastní celá rehoľná komunita alebo eucharistické združenie, prípadne farské spoločenstvo.</li>
                </ul>

                <x-partials.picture titleSlug="pob-024" side="right" dimensionSource="full" columns="5" />

                <p>
                    V&nbsp;Detve sa na Sviatok Božieho tela konala vždy procesia sprevádzaná dychovou hudbou. Na štyroch miestach v&nbsp;uliciach Detvy okolo kostola miestne ženy urobili zelené oltáriky - koľibki, pri ktorých sa konala bohoslužba. Prvá bola na dolnom konci pri feštrovi, druhá pred Lašouskím, treťa pri Floriánkovi a&nbsp;štvrtá pred Vagačovci. (Svedectvo ženy narodenej v&nbsp;r. 1929) Kňaz s&nbsp;hostiou v&nbsp;monštrancii kráčal pod baldachýnom, ktorý niesli štyria chlapi v&nbsp;krojoch. Mládenci niesli vyzdobené zástavy a&nbsp;z&nbsp;nich visiace stuhy niesli dievky oblečené v&nbsp;bielych krojoch. Malé dievčatá posýpali cestu pred kňazom kvetnými lupeňmi. Táto tradícia eucharistickej procesie v&nbsp;blízkosti kostola s&nbsp;ľudovými prvkami v&nbsp;sviatočných krojoch sa v&nbsp;Detve zachovala dodnes.
                </p>

                <x-partials.picture titleSlug="pob-022" side="left" dimensionSource="medium" columns="3" />

                <p>
                    V&nbsp;našom prostredí je zaužívaná aj Prvonedeľná pobožnosť pred vyloženou Oltárnou sviatosťou po „veľkej“ nedeľnej svätej omši o&nbsp;10.30 hod. V&nbsp;prvopiatkovom týždni býva vo štvrtok po večernej svätej omši o&nbsp;17.30 hod. hodinová eucharistická adorácia a&nbsp;v&nbsp;piatok býva vo farskom kostole adorácia od 16.30&nbsp;hod., ktorá je pred večernou svätou omšou ukončená Prvopiatkovou pobožnosťou. Táto pobožnosť pred vyloženou Oltárnou sviatosťou býva na prvý piatok mesiaca taktiež v&nbsp;kláštore bosých karmelitánok po rannej svätej omši o&nbsp;7.00 hod. Počas celého kalendárneho roka býva v&nbsp;kláštornom kostole moderovaná adorácia Najsvätejšej sviatosti vždy v&nbsp;stredu od 19.30 do 20.30 hod.
                </p>
                <p>
                    Jedným z&nbsp;najrozšírenejších a&nbsp;najobľúbenejších prejavov cirkevnej zbožnosti sa stala úcta k&nbsp;Najsvätejšiemu Srdcu Ježišovmu. Foriem úcty k&nbsp;Spasiteľovmu Srdcu je viacero: osobné zasvätenie, zasvätenie rodiny, Litánie k&nbsp;Najsvätejšiemu Srdcu Ježišovmu, Pobožnosť deviatich prvých piatkov mesiaca, ktorá má svoj pôvod vo veľkom prisľúbení, ktoré dal Pán Ježiš sv. Margite Márii Alacoque. Osobitné postavenie má aj uctievanie Pánovej krvi, vyliatej za našu spásu a&nbsp;uvedomovanie si jej nesmiernej hodnoty (napr. Litánie Kristovej krvi).
                </p>
                <p>
                    V&nbsp;povedomí Ježišovej modlitby „aby všetci boli jedno, ako ty, Otče, vo mne a&nbsp;ja v&nbsp;tebe, aby aj oni boli v&nbsp;nás jedno, aby svet uveril, že si ma ty poslal“ (Jn 17,21), sa zvlášť počas Týždňa modlitieb za jednotu kresťanov, od 17. do 25. januára, odporúča spoločná modlitba za dosiahnutie milosti jednoty. Ekumenické stretnutia sa katolíkov s&nbsp;inými kresťanmi patriacimi do iných cirkví a&nbsp;cirkevných spoločenstiev majú slúžiť k&nbsp;predloženiu spoločných potrieb a&nbsp;starostí Bohu, i&nbsp;na vzdávanie vďaky Bohu a&nbsp;spoločné vyprosenie jeho pomoci.
                </p>
