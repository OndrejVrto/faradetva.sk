        <x-web.page.subsection title="Liturgický rok a&nbsp;ľudová zbožnosť">

            <x-web.page.text-segment>

                <x-partials.picture titleSlug="pob-006" side="right" dimensionSource="medium" columns="3" />

                <p>
                    Liturgický rok je časová štruktúra, v&nbsp;rámci ktorej Cirkev slávi celé Kristovo tajomstvo: od Vtelenia a&nbsp;Narodenia Pána až po Nanebovstúpenie, deň Zoslania Ducha Svätého a&nbsp;očakávanie blaženej nádeje a&nbsp;príchodu Pána. V&nbsp;liturgickom roku slávenie veľkonočného tajomstva tvorí privilegovaný moment kresťanského kultu v&nbsp;jeho každodennom, týždennom a&nbsp;ročnom vývoji. Z&nbsp;toho vyplýva, že vo vzťahu medzi liturgiou a&nbsp;ľudovou zbožnosťou sa má pokladať za pevný bod priorita slávení liturgického roka nad každým iným prejavom a&nbsp;úkonom zbožnosti.
                </p>

                <x-partials.picture titleSlug="pob-001" side="left" dimensionSource="small" columns="3" />

                <p>
                    V&nbsp;adventom období očakávania, obrátenia a&nbsp;nádeje patria medzi prejavy ľudovej zbožnosti: Adventný veniec, Zimné kántrové dni, Vianočná novéna - Kto dá prístrešie Svätej rodine? Vo vianočnom období Cirkev slávi tajomstvo Zjavenia Pána, jeho skromného narodenia v&nbsp;Betleheme. Ako je známe, okrem stavania betlehemských jasieľ v&nbsp;kostoloch, počnúc od 13.&nbsp;storočia sa rozšíril zvyk, ovplyvnený bezpochyby jasličkami pripravenými sv. Františkom z&nbsp;Assisi v&nbsp;roku 1223 v&nbsp;Grecciu, stavať malé betlehemy aj v&nbsp;domácich príbytkoch. Ľudovú zbožnosť vo vianočnom čase predstavujú: Betlehemy (aj „živé“), Vianočná (štedrovečerná) večera, Jasličkové pobožnosti, Koledovanie, Koncoročná pobožnosť, Trojkráľové požehnanie príbytkov.
                </p>

                <p>
                    Pôst je obdobie, ktoré predchádza a&nbsp;pripravuje na slávenie Veľkej noci. Je časom počúvania Božieho slova a&nbsp;obrátenia, zmierenia s&nbsp;Bohom a&nbsp;s&nbsp;bratmi, časom modlitby, pôstu a&nbsp;almužny. Začiatok štyridsaťdňového obdobia pokánia sa predstavuje jednoduchým symbolom „popolca“, ktorým sa vyznačuje Popolcová streda. Medzi pobožnosťami, ktorými si veriaci uctievajú Pánovo utrpenie, máloktoré sú také obľúbené ako Krížová cesta. Vo Veľkom týždni svätá Cirkev slávi tajomstvo spásy, zavŕšené Kristom v&nbsp;posledných dňoch jeho života. V&nbsp;tomto období je ľudová zbožnosť viditeľná v&nbsp;úkonoch ako: Návšteva miesta uloženia Najsvätejšej sviatosti na Zelený štvrtok, Predstavenie Kristovho utrpenia - pašiové hry, Lamentácie - Náreky proroka Jeremiáša, Návšteva „Božieho hrobu“. Vo veľkonočnom období sa vo viacerých oblastiach rozšírila pobožnosť Via lucis - cesta svetla, pri ktorej podobne ako pri krížovej ceste veriaci prechádzajú určitou cestou a&nbsp;uvažujú o&nbsp;rozličných zjaveniach Zmŕtvychvstalého Krista. V&nbsp;spojitosti s&nbsp;veľkonočnou oktávou sa rozšírila Pobožnosť k&nbsp;Božiemu milosrdenstvu, ktorá sa rozvinula aj do každodennej osobitnej modlitby Korunka k&nbsp;Božiemu milosrdenstvu. Pred slávnosťou Turíc - Zoslania Ducha Svätého je zaužívaná Novéna k&nbsp;Duchu Svätému.
                </p>
