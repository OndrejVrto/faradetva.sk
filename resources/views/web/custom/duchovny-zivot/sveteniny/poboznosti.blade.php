                <p>
                    Pod pobožnosťami chápeme rozličné vonkajšie prejavy (napr. v&nbsp;textoch modlitieb a&nbsp;piesní, v&nbsp;zachovávaní období a&nbsp;navštevovania osobitných miest, v&nbsp;používaní odznakov, medailí, rúch a&nbsp;zvykov), ktoré oživené vnútorným postojom viery prejavujú zvláštny dôraz vzťahu veriaceho k&nbsp;božským osobám, alebo k&nbsp;Panne Márii v&nbsp;jej privilégiách milosti a&nbsp;v&nbsp;tituloch, ktoré ich vyjadrujú, alebo k&nbsp;svätým, pre ich pripodobnenie sa Kristovi, alebo pre úlohu, ktorú zohrali v&nbsp;Cirkvi.
                </p>
                <p>
                    Rozličné kultové prejavy súkromného alebo komunitného charakteru označujeme výrazom „ľudová zbožnosť“. Tieto prejavy ľudovej zbožnosti sa vyjadrujú prevažne nie prvkami posvätnej liturgie, ale zvláštnymi formami, ktoré vyplývajú z&nbsp;ducha určitého národa alebo určitej etnickej skupiny a&nbsp;jej kultúry. Ľudová zbožnosť je pokladaná za pravý poklad Božieho ľudu. Vyjadruje smäd po Bohu a&nbsp;má veľký zmysel pre hlboké atribúty Boha: jeho otcovstvo, prozreteľnosť, milujúcu a&nbsp;trvalú prítomnosť, trpezlivosť, zmysel pre kríž v&nbsp;každodennom živote, odpútanosť, otvorenosť voči druhým a&nbsp;nábožnosť.
                </p>
                <p>
                    Prejavy ľudovej zbožnosti sa nesmú nahrádzať a&nbsp;ani sa miešať s&nbsp;liturgickými sláveniami Eucharistie. Ľudová nábožnosť nenadväzuje nutne na kresťanské zjavenie, ale v&nbsp;mnohých regiónoch dáva priestor určitému druhu „ľudového katolicizmu“. Znamenitosť liturgie vzhľadom na každú inú možnú a&nbsp;legitímnu formu kresťanskej modlitby musí nájsť súhlas vo svedomí veriacich: kým sviatostné úkony sú potrebné pre život v&nbsp;Kristovi, formy ľudovej zbožnosti patria len do okruhu dobrovoľnosti.
                </p>
