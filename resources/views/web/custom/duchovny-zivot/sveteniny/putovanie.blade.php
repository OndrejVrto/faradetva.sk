                    Typickým a&nbsp;rozšíreným prejavom ľudovej zbožnosti ako všeobecnej náboženskej skúsenosti je putovanie. Púte sú úzko späté so svätyňami a&nbsp;tvoria neodmysliteľnú zložku ich života. Svätyňa, či už je zasvätená Najsvätejšej Trojici, Pánovi Ježišovi, Preblahoslavenej Panne Márii, anjelom, svätým alebo blahoslaveným, je miestom, kde sa majú veriacim hojnejšie poskytovať prostriedky spásy horlivým hlásaním Božieho slova, vhodným rozvíjaním liturgického života, najmä slávením Eucharistie a&nbsp;vysluhovaním sviatosti zmierenia, ako aj pestovaním schválených foriem ľudovej zbožnosti.
                </p>

                <p>
                    Odvtedy, čo Ježiš zavŕšil v&nbsp;sebe samom tajomstvo chrámu a&nbsp;prešiel z&nbsp;tohto sveta k&nbsp;Otcovi, uskutočniac vo svojej osobe definitívny exodus, pre jeho učeníkov už neexistuje nijaké záväzné putovanie: celý ich život je cestou k&nbsp;nebeskej svätyni a&nbsp;sama Cirkev vie, že je „pútnikom na zemi“. V&nbsp;dôsledku identifikácie miesta nájdenia relikvií Pánovho utrpenia vo 4.&nbsp;storočí, okrem putovania kresťanov k&nbsp;hrobom mučeníkov, zaznamenáva kresťanské putovanie obrat: je to práve návšteva Palestíny, ktorá sa svojimi „svätými miestami“ stáva počnúc Jeruzalemom celá Svätou zemou.
                </p>

                <x-partials.picture titleSlug="put-014" side="right" dimensionSource="full" columns="4" />

                <p>
                    Stredovek bol zlatou epochou pre putovanie. Okrem prvoradej náboženskej funkcie púte znamenali mimoriadny prínos pre budovanie západného kresťanstva, pre zjednocovanie rozličných národov a&nbsp;pre vzájomnú výmenu hodnôt rozličných európskych civilizácií. Veľkú duchovnú príťažlivosť má v&nbsp;sebe predovšetkým Jeruzalem, ktorý bol aj počiatkom javu „križiackych výprav“, ktorých inšpirujúcim motívom bolo práve umožnenie návštevy Kristovho hrobu veriacim počas moslimskej okupácie.
                </p>

                <p>
                    Do Ríma chodia pútnici uctiť si pamiatku apoštolov Petra a&nbsp;Pavla, navštíviť katakomby a&nbsp;baziliky, uctiť si službu Petrovho nástupcu v&nbsp;prospech všeobecnej Cirkvi. Veľmi navštevovaným bolo v&nbsp;9.&nbsp;a&nbsp;16. storočí - a&nbsp;je aj dnes - Santiago de Compostela v&nbsp;Španielsku, ku ktorému smerujú z&nbsp;rozličných krajín rozličné trasy, ktoré sa neskôr sformovali ako náboženské, sociálne a&nbsp;charitatívne púte. Spomedzi mnohých ďalších v&nbsp;celom svete možno uviesť známe pútnické miesta: Aparecida, Assisi, Čenstochová, Fatima, Guadalupe, Loreto, Lurdy, Mariazell, Mont-Saint-Michel, Padova, San Giovannie Rotondo atď. Aj na Slovensku je viacero pútnických miest. Medzi najnavštevovanejšie patria Levoča, Šaštín, Marianka, Staré Hory, Trnava, Nitra, Skalka pri Trenčíne, Hronský Beňadik, Banská Štiavnica, Turzovka, Rajecká Lesná, Ladce-Butkov, Ľutina, Litmanová, Klokočov.
                </p>

