                </p>

                <x-partials.picture titleSlug="put-015" side="right" dimensionSource="full" columns="3" />

                <p>
                    Ako je svätyňa miestom modlitby, tak putovanie je cestou modlitby. V&nbsp;každej jeho etape má modlitba oživovať putovanie a&nbsp;Božie slovo má byť jeho svetlom a&nbsp;vodidlom, obživou a&nbsp;oporou. Dobrý výsledok putovania ako kultového prejavu i&nbsp;samotné duchovné plody, ktoré sa od neho očakávajú, sú zabezpečené riadnym vykonávaním bohoslužieb a&nbsp;primeraným dôrazom na rozličné fázy putovania. V&nbsp;minulosti išlo o&nbsp;niekoľkodňovú záležitosť peších pútí, v&nbsp;letnom období sa aj Detvania zúčastňovali procesií z&nbsp;Detvy do Starých Hôr. Dnes sa vo väčšej miere praktizuje putovanie pomocou moderných prepravných prostriedkov. Väčší dôraz sa kladie na spoločné slávenie Eucharistie a&nbsp;rôzne ľudové pobožnosti priamo na pútnickom mieste. Napriek tomu si dnešné púte zachovali svoj význam, stále ich možno charakterizovať ako miesta duchovného obrátenia a&nbsp;posilnenia viery v&nbsp;Boha, ale aj prostriedku pokánia a&nbsp;obety.
