                <div class="d-block ms-2 ms-xl-5">
                    <strong>Poznáme päť základných foriem modlitby:</strong>
                    <ul>
                        <li>velebenie</li>
                        <li>klaňanie (adorácia)</li>
                        <li>prosebná modlitba (modlitba príhovoru)</li>
                        <li>modlitba vďakyvzdania</li>
                        <li>modlitba chvály</li>
                    </ul>
                </div>

                <p>
                    Velebenie je odpoveď človeka na Božie dary. Pretože Boh nám dáva požehnanie, srdce človeka môže zasa velebiť toho, kto je zdrojom každého požehnania. Jeho dobrota, blízkosť, zľutovanie - to všetko znamená požehnanie. Každý kresťan by mal prosiť o&nbsp;Božie požehnanie pre seba i&nbsp;pre druhých ľudí. Rodičia môžu požehnať svoje deti znamením kríža na čele. Ľudia, ktorí sa majú radi, sa môžu vzájomne žehnať. Okrem toho kňaz z&nbsp;moci svojho úradu žehná vyslovene v&nbsp;Ježišovom mene a&nbsp;z&nbsp;poverenia Cirkvi. Jeho prosba o&nbsp;požehnanie nadobúda zvláštnu účinnosť mocou kňazskej vysviacky a&nbsp;mocou modlitby celej Cirkvi.
                </p>

                <x-partials.picture titleSlug="modl-014" side="left" columns="3" />

                <p>
                    Každý človek, ktorý si uvedomuje, že je Božím stvorením, pokorne uznáva Boha, svojho Stvoriteľa a&nbsp;klania sa mu. V&nbsp;postoji klaňania (adorácia) však neuctieva iba Božiu veľkosť, všemohúcnosť a&nbsp;svätosť, ale skláňa sa aj pred Božou láskou, ktorá spôsobila, že sa v&nbsp;Ježišovi Kristovi Boh stal človekom. Ten, kto sa skutočne klania Bohu, padá pred ním na kolená alebo sa pred ním vrhá na zem. Vyjadruje tak pravdivosť vzťahu medzi Bohom a&nbsp;človekom: Boh je veľký, my sme malí. A&nbsp;pritom človek nie je nikdy väčší ako vtedy, keď v&nbsp;slobodnej odovzdanosti padá na kolená pred Bohom. Človek, ktorý hľadá Boha a&nbsp;začína sa modliť, môže takýmto spôsobom nájsť k&nbsp;nemu cestu.
                </p>

                <p>
                    Boh, ktorý nás pozná do hĺbky duše, vie, čo potrebujeme. A&nbsp;predsa si praje, aby sme ho prosili, aby sme sa na neho obracali vo svojich potrebách, aby sme na neho volali a&nbsp;utiekali sa k&nbsp;nemu, aby sme sa mu vyžalovali, vzývali ho a&nbsp;dokonca s&nbsp;ním zápasili. Boh nepotrebuje naše prosby, aby nám pomáhal. To, že ho máme prosiť, je pre naše dobro. Ten, kto neprosí a&nbsp;nechce prosiť, uzatvára sa do seba. Len človek, ktorý prosí, sa otvára a&nbsp;obracia sa na Pôvodcu všetkého dobra. Ten, kto prosí, vracia sa domov k&nbsp;Bohu. Preto prosebná modlitba uvádza človeka do správneho vzťahu k&nbsp;Bohu, ktorý rešpektuje našu slobodu.
                </p>

                <x-partials.picture titleSlug="modl-004" side="right" columns="2" />

                <p>
                    Čím viac sa človek učí modliť, tým hlbšie prežíva, že je začlenený do duchovnej rodiny, v&nbsp;ktorej sa prejavuje sila modlitby. So všetkou starostlivosťou o&nbsp;ľudí, ktorých milujeme, stojíme uprostred ľudskej rodiny, v&nbsp;ktorej môžeme čerpať silu z&nbsp;modlitby druhých a&nbsp;môžeme tiež zvolávať na druhých pomoc.
                </p>

                <p>
                    Všetko, čím sme a&nbsp;čo máme, dostávame od Boha. Ďakovať Bohu, darcovi všetkého dobra, nás robí šťastnými. Bohu vzdávame vďaky neustále, predovšetkým však slávením Eucharistie (gréc. vďakyvzdanie). V&nbsp;nej Ježiš prináša Bohu celé premenené stvorenie a&nbsp;dáva nám účasť na svojom vďakyvzdaní Otcovi. Aj nás Ježiš vykúpil z&nbsp;moci hriechu a&nbsp;smrti a&nbsp;premieňa nás, preto máme byť z&nbsp;hĺbky svojho srdca vďační a&nbsp;vyjadrovať Bohu svoje vďaky najrôznejšími spôsobmi.
                </p>

                <p>
                    Boh nepotrebuje náš potlesk. Naopak, my potrebujeme spontánne prejaviť Bohu svoju radosť a&nbsp;nadšenie. Chválime Boha pre neho samého, že existuje a&nbsp;že je dobrý. Tak sa už teraz pripájame k&nbsp;večnej chvále anjelov a&nbsp;svätých v&nbsp;nebi.
                </p>

