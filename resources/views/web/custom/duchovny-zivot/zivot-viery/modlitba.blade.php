                <p>
                    Modlitba je povznesenie duše k&nbsp;Bohu. Keď sa človek modlí, vstupuje do živého vzťahu s&nbsp;Bohom. Modlitba je veľkou bránou k&nbsp;viere. Ten, kto sa modlí, nežije už viac zo seba, pre seba a&nbsp;z&nbsp;vlastných síl. Vie, že jestvuje Boh, s&nbsp;ktorým sa môže rozprávať a&nbsp;čoraz viac sa mu zverovať. Už teraz hľadá kontakt s&nbsp;tým, s&nbsp;ktorým sa jedného dňa stretne z&nbsp;tváre do tváre. Preto k&nbsp;životu kresťana patrí každodenná modlitba. Modlitbe sa však nemožno naučiť tak ako nejakej technike. Modlitba je totiž dar, ktorý človek dostáva práve tým, že sa modlí.
                </p>

                <x-partials.picture titleSlug="modl-005" side="right" columns="3" />

                <p>
                    Modlíme sa, pretože nás napĺňa nekonečná túžba po Bohu a&nbsp;pretože Boh stvoril človeka pre seba. Často ale na Boha zabúdame, utekáme pred ním a&nbsp;skrývame sa. No i&nbsp;keď sa vyhýbame myšlienke na Boha alebo ho zapierame - on je tu vždy pre nás. Skôr ako ho začneme hľadať, hľadá on nás, túži po nás, volá nás. Človek sa rozpráva so svojím svedomím a&nbsp;zrazu zistí, že hovorí s&nbsp;Bohom. Keď sa cíti osamelý a&nbsp;nemá sa s&nbsp;kým porozprávať, zrazu zistí, že s&nbsp;Bohom sa môže kedykoľvek zhovárať. Keď sa ocitne v&nbsp;nebezpečenstve, zistí, že Boh odpovedá na jeho volanie o&nbsp;pomoc. Modlitba je človeku taká blízka ako dýchanie, jedlo, láska. Modlitba očisťuje. Modlitba umožňuje zdolávať pokušenia a&nbsp;posilňuje v&nbsp;slabosti. Modlitba odníma strach, znásobuje sily, upevňuje vytrvalosť. Modlitba prináša šťastie.
                </p>

                <p>
                    Učiť sa modlitbe od Ježiša znamená vstúpiť do jeho bezhraničnej dôvery, pripojiť sa k&nbsp;jeho modlitbe a&nbsp;nechať sa ním krok za krom viesť k&nbsp;Otcovi. Učeníci, ktorí žili v&nbsp;spoločenstve s&nbsp;Ježišom, učili sa od neho modliť tým, že ho počúvali a&nbsp;napodobňovali ho. Tak ako on museli byť aj oni bdelí, museli zápasiť o&nbsp;čisté srdce, vzdávať sa všetkého pre Božie kráľovstvo, odpúšťať nepriateľom, nadovšetko dôverovať Bohu a&nbsp;lásku k&nbsp;nemu klásť na prvé miesto. Príkladom svojej odovzdanosti nabádal Ježiš svojich učeníkov, aby všemohúceho Boha oslovovali „Abba, Otče“. Keď sa modlíme v&nbsp;Ježišovom Duchu, predovšetkým modlitbu Otče náš, kráčame v&nbsp;Ježišových stopách a&nbsp;môžeme si byť istí, že nepochybne prídeme do Otcovho náručia.
                </p>

