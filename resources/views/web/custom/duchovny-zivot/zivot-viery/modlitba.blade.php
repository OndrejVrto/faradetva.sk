    <x-web.page.section name="PAGE: ({{ $pageData->title }}) PART II -" class="static-page pad_t_50 pad_b_50">

        <x-web.page.subsection title="Pramene modlitby" animation="fromleft">
            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="modl-016" side="left" columns="2" />

                <p>
                    Biblia je pre modlitbu ako vyvierajúci prameň. Modliť sa Božím slovom znamená využívať biblické slová a&nbsp;udalosti pre vlastnú modlitbu. V&nbsp;bohoslužbe Cirkvi, v&nbsp;jej dennej modlitbe (Liturgia hodín) a&nbsp;vo svätej omši ľudia spoločne recitujú modlitby čerpané zo Svätého písma alebo z&nbsp;tradície Cirkvi. Tieto modlitby spájajú jednotlivca s&nbsp;modliacim sa spoločenstvom Cirkvi. Kresťanská modlitba nie je súkromná záležitosť, ale je veľmi osobná. Osobná modlitba sa očisťuje, rozširuje a&nbsp;posilňuje, keď pravidelne vteká do prúdu modlitieb celej Cirkvi. Keď sa veriaci ľudia na celom svete zjednotia v&nbsp;modlitbe rovnakými slovami v&nbsp;rovnakom čase, a&nbsp;tak Bohu spievajú jediný chválospev, je to veľké a&nbsp;krásne znamenie.
                </p>

                <x-partials.picture titleSlug="modl-020" side="right" columns="4" />

                <p>
                    Kresťanská modlitba je modlitbou viery, nádeje a&nbsp;lásky. Je vytrvalá a&nbsp;odovzdaná do Božej vôle. Kto sa modlí ako kresťan, vychádza v&nbsp;tom okamihu zo seba a&nbsp;zaujíma postoj viery a&nbsp;dôvery v&nbsp;jediného Boha a&nbsp;Pána; zároveň celú svoju nádej skladá v&nbsp;Boha: vie, že ho Boh počúva, rozumie mu, prijíma ho a&nbsp;napĺňa. Kresťanská modlitba je prejavom lásky, ktorá pramení z&nbsp;Kristovej lásky a&nbsp;ktorá túto Božiu lásku hľadá. Každá udalosť, každé stretnutie sa môže stať podnetom k&nbsp;modlitbe. Čím viac prežívame jednotu s&nbsp;Bohom, tým hlbšie rozumieme svetu okolo nás.
                </p>

                <p>
                    Ten, kto vyhľadáva jednotu s&nbsp;Ježišom hneď ráno, môže žehnať ľudí, ktorých stretne, dokonca aj svojich odporcov a&nbsp;nepriateľov. Počas dňa zloží všetky svoje starosti na Pána. Svoje rozhodnutia a&nbsp;úsudky robí tak, že sa najprv pýta, akoby na jeho mieste konal Ježiš. Strach prekonáva vďaka tomu, že si uvedomuje Božiu blízkosť. V&nbsp;zúfalých situáciách nachádza oporu v&nbsp;Bohu. Má v&nbsp;sebe nebeský pokoj a&nbsp;vnáša ho do sveta. Je vďačný a&nbsp;raduje sa z&nbsp;pekných vecí, ale dokáže znášať aj ťažkosti, ktoré ho stretnú. Takto môže vnímať Boha dokonca aj pri práci.
                </p>

                <x-partials.picture titleSlug="modl-001" side="left" columns="4" />

                <p>
                    Modliť sa k&nbsp;Bohu môže človek jedine s&nbsp;Bohom. Ježiš bytostne túžil po zjednotení s&nbsp;Otcom: navzájom sa milovať, navzájom sa počúvať, dokonale si rozumieť, chcieť len to, čo chce ten druhý. Ako kresťania sme prijali Ježišovho Ducha, ktorý nás priťahuje na cestu modlitby, predchádza nás svojou milosťou, kedykoľvek sa začíname modliť a&nbsp;hovorí cez nás. Modlitba teda v&nbsp;zásade znamená, že z&nbsp;hĺbky môjho srdca hovorí Boh k&nbsp;Bohu. Duch Svätý pomáha nášmu duchu modliť sa. Preto by sme mali vždy znova opakovať: „Príď Duchu Svätý, a&nbsp;pomôž mi modliť sa.“
                </p>

                <p>
                    Modliť sa môžeme úplne všade. Napriek tomu katolík vyhľadáva tiež miesta, kde Boh „prebýva“ osobitným spôsobom. Sú to predovšetkým katolícke kostoly, kde je náš Pán prítomný sviatostným spôsobom vo svätostánku. Je však dôležité modliť sa všade: v&nbsp;škole, v&nbsp;autobuse, na večierku, medzi priateľmi. Celý svet musí byť preniknutý požehnaním. Rovnako dôležité však je navštevovať posvätné miesta, na ktorých na nás v&nbsp;určitom zmysle čaká Boh, aby sme si pri ňom mohli odpočinúť, načerpať silu, nechať sa naplniť a&nbsp;poslať. Keď kresťan vstúpi do nejakého kostola, nikdy v&nbsp;ňom nekoná iba turistickú prehliadku. Na okamih spočinie v&nbsp;tichu, pokloní sa Bohu a&nbsp;obnoví tak svoje priateľstvo a&nbsp;lásku k&nbsp;nemu.
                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">
                    Cesta modlitby
                </h3>

                <x-partials.picture titleSlug="modl-019" side="right" columns="3" />

                <p>
                    Kresťanská tradícia zachovala tri spôsoby modlitby: ústnu modlitbu, rozjímavú modlitbu a&nbsp;vnútornú modlitbu. Všetky tri spôsoby modlitby vyžadujú sústredenosť srdca.
                </p>

                <x-partials.picture titleSlug="modl-006" side="left" columns="2" />

                <p>
                    Modlitba je v&nbsp;prvom rade pozdvihnutie srdca k&nbsp;Bohu. A&nbsp;predsa aj Ježiš učil modliť sa slovami. V&nbsp;modlitbe Otče náš nám zanechal dokonalú ústnu modlitbu ako svoj odkaz, aby sme vedeli, ako sa máme modliť. Pri modlitbe sa nemáme iba oddávať zbožným myšlienkam. Mali by sme vysloviť všetko, čo máme na srdci a&nbsp;predniesť to Bohu ako nárek, prosbu, chválu a&nbsp;vďaku. Často práve veľké ústne modlitby - Žalmy a&nbsp;hymny zo Svätého písma, Otče náš, Zdravas' Mária, Sláva Otcu - poukazujú na skutočný obsah modlitby a&nbsp;vedú nás k&nbsp;slobodnej vnútornej modlitbe.
                </p>

                <p>
                    Podstatou rozjímavej modlitby je hľadanie a&nbsp;uvažovanie, ktoré vychádza z&nbsp;Božieho slova vo Svätom písme alebo z&nbsp;obrazu. S&nbsp;ich pomocou človek pátra po Božej vôli, znamení a&nbsp;prítomnosti. Človek nemôže „čítať“ posvätné texty a&nbsp;obrazy tak, ako číta napríklad v&nbsp;dennej tlači o&nbsp;udalostiach, ktoré sa ho bezprostredne netýkajú. Nad týmito textami a&nbsp;obrazmi by mal rozjímať, to znamená, že by mal pozdvihnúť svoje srdce k&nbsp;Bohu a&nbsp;povedať mu, že je teraz úplne otvorený na to, čo mu Boh chce povedať prostredníctvom toho, čo prečítal alebo videl.
                </p>

                <p>
                    Vnútorná (kontemplatívna) modlitba je láska, mlčanie, počúvanie a&nbsp;zotrvávanie pred Bohom. Vyžaduje si čas, odhodlanie a&nbsp;predovšetkým čisté srdce. Ide o&nbsp;jednoduchý pohľad na Boha v&nbsp;mlčaní a&nbsp;láske, o&nbsp;pokorné, chudobné odovzdanie sa, pri ktorom človek odloží všetky masky a&nbsp;celým srdcom hľadá Boha. Vnútorná modlitba sa často nazýva aj modlitbou srdca.
                </p>

                <x-partials.picture titleSlug="modl-010" side="left" columns="3" />

                <p>
                    Modlitba je osobným vzťahom, prechádza od „ja“ k&nbsp;„ty“, od zamerania sa na seba k&nbsp;radikálnej otvorenosti. Ten, kto sa naozaj modlí, zistí, že Boh hovorí a&nbsp;že často hovorí inak, akoby sme si to želali alebo očakávali. Tí, ktorí majú hlbšie skúsenosti s&nbsp;modlitbou, dosvedčujú, že človek je po skončení modlitby často iný, než aký bol na jej začiatku. Naše očakávania sa niekedy naplnia: smutný nájde útechu, skleslý dostane novú silu. Môže sa však stať aj to, že človek by rád zabudol na ťažkosti a&nbsp;upadne ešte do väčšej úzkosti; chcel by mať na chvíľu pokoj, a&nbsp;dostane úlohu. Skutočné stretnutie s&nbsp;Bohom v&nbsp;modlitbe môže rozmetať naše predstavy o&nbsp;Bohu i&nbsp;o&nbsp;modlitbe.
                </p>

                <p>
                    Modlitba nehľadá povrchný úspech, ale Božiu vôľu a&nbsp;blízkosť. Práve v&nbsp;zdanlivom mlčaní Boha spočíva pozvanie k&nbsp;ďalšiemu kroku - k&nbsp;bezvýhradnej odovzdanosti, k&nbsp;bezhraničnej viere, k&nbsp;nekonečnému očakávaniu. Ten, kto sa modlí, musí ponechať Bohu úplnú slobodu, aby prehovoril, kedy bude chcieť, aby splnil, čo bude chcieť a&nbsp;aby sa daroval, ako bude chcieť. Boha nemožno odbiť niekoľkými slovami ráno alebo večer. Náš život sa musí stať modlitbou a&nbsp;naša modlitba sa musí stať životom. Životný príbeh každého kresťana je aj príbehom modlitby, je to jediný dlhodobý pokus o&nbsp;čoraz hlbšiu jednotu s&nbsp;Bohom. Modlitba je životne dôležitá, preto modlitbu a&nbsp;život nemožno od seba oddeliť.
                </p>

