        <x-web.page.subsection title="II. Zachovávať prikázané dni pokánia" animation="fromleft">
                <x-web.page.text-segment animation="fromleft">
                <p>
                    Konať skutky kajúcnosti každý piatok a na Popolcovú stredu zabezpečuje obdobia askézy a pokánia, ktoré nás pripravujú na liturgické sviatky a prispieva k tomu, že získavame nadvládu nad svojimi pudmi a slobodu srdca. Pokánie sa prejavuje veľmi rozmanitými formami, najmä pôstom, modlitbou a almužnou. Tieto a mnohé iné formy pokánia môže kresťan praktizovať v každodennom živote, najmä v Pôstnom období a v piatky, dni kajúcnosti.
                </p>
                <p>
                    Všetky piatky v roku sú dňami pokánia. Veriaci majú povinnosť konať niektorý z týchto druhov pokánia:
                        <ol>
                            <li>zdržovať sa mäsitých pokrmov</li>
                            <li>vykonať skutok nábožnosti (svätá omša, bolestný ruženec, krížová cesta)</li>
                            <li>čítanie Svätého písma aspoň 10 minút</li>
                            <li>navštíviť chorého s konkrétnym prejavom pomoci</li>
                            <li>vykonať sebazápor (napr. zrieknuť sa televízie, fajčenia, alkoholu a pod.)</li>
                        </ol>
                </p>
                <p>
                    Popolcová streda a Veľký piatok sú dni prísneho pôstu, t. j. v tento deň je zdržiavanie sa mäsitého pokrmu a len raz za deň je možné dosýta sa najesť. Tento pôst viaže od 18. do 60. roku života. Veriacich od 14. do 18. roku a veriacich nad 60 rokov viaže iba zdržiavanie sa mäsitých pokrmov. Deti do 14. roku života neviaže žiaden pôst, avšak v kresťanskej výchove treba deti viesť k pôstu. Chorých a prípadne fyzicky ťažko pracujúcich neviaže zdržiavanie sa od jedla. V tieto dva dni sa zdržiavanie mäsitých pokrmov nedá nahradiť iným skutkom pokánia.
                </p>
