        <x-web.page.subsection title="Človek je „schopný“ Boha">
            <x-web.page.text-segment>

                <x-partials.picture titleSlug="viera-004" side="right" dimensionSource="medium" columns="4" />

                <p>
                    Túžba po Bohu je vpísaná do srdca človeka, lebo človek je stvorený Bohom a&nbsp;pre Boha. Boh neprestáva priťahovať človeka k&nbsp;sebe a&nbsp;jedine v&nbsp;Bohu nájde človek pravdu a&nbsp;šťastie, ktoré neprestajne hľadá. Ľudia vo svojich dejinách až podnes mnohorakým spôsobom vyjadrili toto hľadanie Boha svojím náboženským presvedčením a&nbsp;náboženskými prejavmi (modlitbou, obetami, kultom, rozjímaním atď.). Hoci tieto formy vyjadrenia môžu obsahovať nejasnosti, sú také univerzálne, že človeka možno nazvať náboženskou bytosťou.
                </p>
                <p>
                    Človek stvorený na Boží obraz a&nbsp;povolaný k&nbsp;tomu, aby Boha poznal a&nbsp;miloval, keď hľadá Boha, objavuje určité „cesty“, aby došiel k&nbsp;poznaniu Boha. Volajú sa aj „dôkazmi jestvovania Boha“, nie však v&nbsp;zmysle dôkazov, aké hľadajú prírodné vedy, ale ako „zhodné a&nbsp;presvedčivé argumenty“, ktoré umožňujú nadobudnúť opravdivú istotu. Východiskovým bodom týchto „ciest“ priblíženia sa k&nbsp;Bohu je stvorenie: hmotný svet a&nbsp;ľudská osoba.
                </p>
                <p>
                    Svet a&nbsp;človek dosviedčajú, že v&nbsp;sebe samých nemajú ani svoj začiatok, ani posledný cieľ, ale majú účasť na Bytí, ktoré je samo osebe (Esse in se) a&nbsp;nemá začiatok ani koniec. Takto, rozličnými „cestami“ môže človek dôjsť k&nbsp;poznaniu, že jestvuje skutočnosť, ktorá je prvou príčinou a&nbsp;posledným cieľom všetkého a&nbsp;ktorú všetci volajú Boh.
                </p>
                <p>
                    Človek má schopnosti, ktoré mu umožňujú poznať jestvovanie osobného Boha. Aby sa však mohol dostať do dôverného vzťahu s&nbsp;Bohom, chcel sa mu Boh zjaviť a&nbsp;darovať mu milosť, aby mohol toto zjavenie vierou prijať. Dôkazy jestvovania Boha môžu na vieru pripraviť a&nbsp;pomáhať pochopiť, že viera nie je v&nbsp;rozpore s&nbsp;ľudským rozumom. Keď človek počúva posolstvo stvorení a&nbsp;hlas svojho svedomia, môže nadobudnúť istotu o&nbsp;jestvovaní Boha, ktorý je príčinou a&nbsp;cieľom všetkého.
                </p>
                <p>
                    Cirkev učí, že jediného a&nbsp;pravého Boha, nášho Stvoriteľa a&nbsp;Pána, možno s&nbsp;istotou poznať z&nbsp;jeho diel prirodzeným svetlom ľudského rozumu. O&nbsp;Bohu môžeme skutočne hovoriť, vychádzajúc z&nbsp;mnohorakých dokonalostí stvorení, ktoré sú podobami nekonečne dokonalého Boha, hoci naša obmedzená reč nevyčerpáva jeho tajomstvo.
                </p>
            </x-web.page.text-segment>
        </x-web.page.subsection>

