                </p>

                <x-partials.picture titleSlug="etik-004" side="left" columns="3" />

                <p>
                    Nikto nemôže namietať, keď niekto zostane stáť vonku pred dverami kostola vtedy, ak sa dnu nezmestí, alebo má malé dieťa, ktoré by ostatných vyrušovalo, alebo ak je vo vnútri vydýchaný vzduch. Ale zúčastniť sa svätej omše tak, že ani nevojdem do kostola, iba stáť neďaleko neho v&nbsp;okruhu niekoľkých desiatok metrov, s&nbsp;niekým debatovať či fajčiť, je nemysliteľné. To potom možno sedieť v&nbsp;každom pohostinstve pri kostole a&nbsp;človek bude účastný na svätej omši? Určite nie.
