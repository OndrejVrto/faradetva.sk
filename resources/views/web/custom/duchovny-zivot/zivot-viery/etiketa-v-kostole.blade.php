                </p>

                <p>
                    Mokrý dáždnik v&nbsp;nijakom prípade nekladieme pod nohy inému človeku. Odporúča sa vložiť mokrý dáždnik, ak je skladací, do igelitovej tašky alebo vrecúška. Takto zabalený ho môžeme dať aj do kabelky. Keď máme veľký dáždnik, ktorý sa nevmestí do igelitovej tašky, alebo ju nemáme so sebou, treba starostlivo zvažovať, kam ho v&nbsp;kostole položíme či oprieme. Na lavicu alebo drevenú podlahu mokrý dáždnik nepatrí.
                </p>

                <p>
                    Kabelka podľa spoločenskej etikety nepatrí na zem ani na stôl. Ak na laviciach nie sú háčiky, treba ju položiť vedľa seba na lavicu alebo ju držať v&nbsp;lone. V&nbsp;kostole by sme nemali mať na sebe slnečné okuliare, tie do miestnosti nepatria. Ani na očiach, ani na vrchu hlavy ako čelenku. Treba ich mať v&nbsp;puzdre. Tvrdenie, že na vrchu hlavy sú ozdoba a&nbsp;módny doplnok, z&nbsp;pohľadu spoločenskej etikety neobstojí. No keď je človek chorý, môže mať slnečné okuliare aj v&nbsp;miestnosti.
                </p>

                <x-partials.picture titleSlug="etik-009" side="right" columns="4" />

                <p>
                    Pokrývka hlavy, zvlášť u&nbsp;mužov, nie je v&nbsp;miestnosti žiaduca. Ak je však človek chorý alebo ide o&nbsp;malé dieťa a&nbsp;rodič sa bojí, aby neochorelo, môže mať pokrývku hlavy. V&nbsp;našom kultúrnom kontexte by dámy nemali do kostola nosiť obrovské klobúky. Tie sú vhodné skôr na dostihy. Každý zážitok, aj zážitok zo svätej omše, je vtedy pozitívny, keď sa do jeho vnímania zapojí viac zmyslov - ak nielen počujeme, ale aj vidíme a&nbsp;pritom nás neruší silný zápach či parfum, alebo obrovský klobúk zasahujúci do nášho teritória.
                </p>

                <p>
                    Nie je správne, keď kňaz aj s&nbsp;miništrantami po svätej omši odchádzajú, a&nbsp;hoci sú ešte stále pri oltári, ľudia sa postavia a&nbsp;idú preč. Pripomína to záver divadelného predstavenia, keď niektorí diváci utekajú do šatne po kabáty. Kam sa náhlia? Nedokážu vychutnať doznievanie povznášajúcich pocitov? Podľa etikety najskôr odchádza kňaz s&nbsp;miništrantami a&nbsp;až po nich veriaci.
