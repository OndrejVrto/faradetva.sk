                </p>

                <p>
                    Sedieť v&nbsp;kostole treba spoločensky vhodne a&nbsp;uvoľnene, nie upäto. Nohy by mali byť pod kľačadlom. Kľačadlo je na kľačanie, preto sa aj tak volá, nie je to opierka na nohy. Avšak keď niektorí ľudia „kľačia“, kolená majú na kľačadle, zadnou časťou tela sa ale opierajú o&nbsp;lavicu, na ktorej predtým sedeli, to nie je kľačanie. Kľačanie je, keď sa celou váhou tela opieram o&nbsp;kolená a&nbsp;nepodopieram sa sedacou časťou tela o&nbsp;lavicu. Ak má človek choré kĺby alebo akékoľvek iné zdravotné problémy, môže zostať sedieť. Nemusí stáť ani počas tých častí svätej omše, pri ktorých sa státie vyžaduje. Keď ma totiž niečo bolí, koncentrujem sa na bolesť, a&nbsp;v&nbsp;kostole by sme sa mali koncentrovať na stretnutie s&nbsp;Pánom.
                </p>

                <p>
                    Do chrámov prichádzajú aj ľudia, ktorí sú zvyknutí na svoje „stále“ miesto v&nbsp;kostole. Nárokovať si na lavicu v&nbsp;kostole súvisí s&nbsp;tým, že človek sa na istom mieste cíti príjemne, akoby bol doma. Tak ako si v&nbsp;obývačke obsadia niektorí ľudia kreslo alebo diaľkový ovládač k&nbsp;televízoru, tak si daktorí vo svojej mysli zarezervujú aj verejné priestranstvo - lavičku v&nbsp;parku alebo sedadlo v&nbsp;kostole, ktoré sa tým stáva ich osobným územím. Ak niekto iný obsadí toto ich teritórium, vyvolá v&nbsp;nich napätie, nepokoj, nervozitu. Takto uvažujúci človek nebude spokojný, kým nebude sedieť na svojom mieste. Na druhej strane však nemá právo vynútiť si ho. Menovky ani rezervácie na miesta v&nbsp;kostoloch nejestvujú, nie sme v&nbsp;divadle ani v&nbsp;reštaurácii. V&nbsp;sporoch o&nbsp;miesto na sedenie spravidla ustúpi ten, kto je ľudskejší a&nbsp;empatickejší. Nech už sú pohnútky niektorých akékoľvek, kostol ani sväté omše nie sú nato, aby sme sa sporili, kde budeme sedieť či stáť, a&nbsp;ani nato, aby sme si vyhradzovali miesto na sedenie či státie.
                </p>

                <p>
