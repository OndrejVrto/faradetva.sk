                <p>
                    Krst je základná sviatosť, brána, ktorá otvára prístup k&nbsp;ostatným sviatostiam.  Zjednocuje nás s&nbsp;Ježišom Kristom, vťahuje nás do jeho vykupiteľskej smrti na kríži, a&nbsp;tak nás vyslobodzuje z&nbsp;moci hriechu, očisťuje od všetkých osobných hriechov a&nbsp;umožňuje nám spolu s&nbsp;ním vstať z&nbsp;mŕtvych k&nbsp;životu, ktorý sa nekončí. Pretože krst je zmluvou s&nbsp;Bohom, človek musí povedať svoje slobodné „áno“. V&nbsp;prípade malých detí vyznávajú vieru ich rodičia ako zástupcovia.
                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">Ako sa udeľuje krst</h3>

                <p>
                    Klasická forma krstu prebieha tak, že ten, kto prijíma krst, sa tri razy ponorí do krstnej vody. Väčšinou sa však tomu, kto prijíma krst, tri razy leje voda na hlavu, pričom ten, kto udeľuje krst, hovorí: „(Meno), ja ťa krstím v&nbsp;mene Otca i&nbsp;Syna i&nbsp;Ducha Svätého.“ Voda symbolizuje očistenie a&nbsp;nový život. Túto skutočnosť vyjadroval už Jánov krst pokánia. Krst, ktorý sa udeľuje v&nbsp;„mene Otca i&nbsp;Syna i&nbsp;Ducha Svätého“ vodou, je viac ako znakom obrátenia a&nbsp;pokánia. Je to nový život v&nbsp;Kristovi. a&nbsp;preto ešte nasleduje pomazanie olejom, oblečenie bieleho rúcha a&nbsp;zapálenie krstnej sviece.
                </p>

            </x-web.page.text-segment>
        </x-web.page.subsection>

        <x-web.page.subsection title="Kto môže byť pokrstený a&nbsp;čo sa vyžaduje od čakateľa na krst">
            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="sacr1-006" side="left" dimensionSource="medium" columns="4" />

                <p>
                    Krst môže prijať každý človek, ktorý ešte nie je pokrstený. Jediným predpokladom krstu je viera, ktorá sa pri krste musí verejne vyznať. Človek, ktorý chce prijať kresťanskú vieru, nemení svoj pohľad na svet. Vstupuje totiž na cestu učenia sa (katechumenát), na ktorej sa pripravuje prijať Boží dar v&nbsp;krste. Osobným obrátením a&nbsp;krstom sa stáva novým človekom, živým údom Kristovho tela - Cirkvi. Tí, čo podstúpia smrť pre vieru, katechumeni a&nbsp;všetci ľudia, ktorí síce nepoznajú Cirkev, ale pod vplyvom milosti úprimne hľadajú Boha a&nbsp;usilujú sa plniť jeho vôľu, môžu sa spasiť, aj keď neprijali krst.
                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">Prečo Cirkev krstí malé deti</h3>

                <x-partials.picture titleSlug="sacr1-001" side="right" dimensionSource="medium" columns="3" />

                <p>
                    Cirkev sa od svojho počiatku drží tradície krstiť malé deti. Má to jediný dôvod: skôr ako sa my rozhodneme pre Boha, Boh sa už dávno rozhodol pre nás. Krst je teda milosť, nezaslúžený dar Boha, ktorý sa nás bezvýhradne ujíma. Preto veriaci rodičia, ktorí si želajú pre svoje dieťa to najlepšie, žiadajú krst, v&nbsp;ktorom sa dieťa vyslobodzuje z&nbsp;vplyvu dedičného hriechu a&nbsp;z&nbsp;moci smrti. Krst detí predpokladá, že kresťanskí rodičia budú pokrstené dieťa uvádzať do viery a&nbsp;do kresťanského života. Ten, kto dieťaťu odopiera krst pre nesprávne chápanú liberálnosť, dopúšťa sa na ňom bezprávia. Tak ako nemožno dieťaťu odoprieť lásku, pre ktorú sa samo tiež dokáže rozhodnúť až neskôr, nemali by veriaci rodičia odopierať svojmu dieťaťu Božiu milosť v&nbsp;krste, ak sa nechcú na ňom dopustiť príkoria. Tak ako sa každý človek rodí so schopnosťou rozprávať, čo sa však musí naučiť, tak sa každý človek rodí i&nbsp;so schopnosťou veriť, musí sa však viere učiť. Krst však nemožno nikomu nanútiť.
                </p>

                <p>
                    Ak bol niekto pokrstený ako malé dieťa, musí svoj krst neskôr „ratifikovať“ vlastným životom - to znamená, že musí k&nbsp;nemu povedať z&nbsp;vlastnej vôle svoje „áno“, aby mohol začať prinášať ovocie.
                </p>

