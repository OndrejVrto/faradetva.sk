            <x-web.page.text-segment animation="fromright">

                <p>
                    Pri krste sa stávame údmi Kristovho tela, bratmi a&nbsp;sestrami nášho Spasiteľa a&nbsp;Božími deťmi. Sme oslobodení od hriechu, vytrhnutí z&nbsp;moci smrti a&nbsp;od tohto okamihu sme predurčení k&nbsp;životu v&nbsp;radosti vykúpených. Byť pokrstený znamená ponoriť svoj osobný životný príbeh do prúdu Božej lásky. Krst vtláča do duše nezmazateľný duchovný znak, charakter, ktorý určuje pokrsteného na kresťanský náboženský kult. Pre tento charakter nemožno krst opakovať. Pokiaľ ide o&nbsp;deti, ktoré zomreli bez krstu, liturgia Cirkvi nás povzbudzuje, aby sme dôverovali v&nbsp;Božie milosrdenstvo a&nbsp;modlili sa za ich spásu.
                </p>

