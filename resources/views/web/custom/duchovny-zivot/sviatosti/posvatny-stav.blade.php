                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">
                    Účinok biskupskej vysviacky
                </h3>

                <p>
                    Biskupskou vysviackou sa udeľuje plnosť sviatosti posvätného stavu. Po vysvätení sa biskup stáva právoplatným nástupcom apoštolov a&nbsp;vstupuje do biskupského zboru. Od okamihu svojho vysvätenia je zodpovedný za celú Cirkev spolu s&nbsp;ostatnými biskupmi a&nbsp;s&nbsp;pápežom.
                </p>

                <x-partials.picture titleSlug="sacr6-006" side="left" dimensionSource="medium" columns="3" />

                <p>
                    Biskupskou vysviackou sa udeľuje poslanie učiť, posväcovať a&nbsp;spravovať. Biskup, ktorý spolu s&nbsp;kňazmi a&nbsp;diakonmi ako so svojimi vysvätenými spolupracovníkmi vykonáva pastiersku službu, je viditeľným princípom a&nbsp;základom miestnej cirkvi (biskupstva/diecézy).
                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">
                    Ako sa odlišuje všeobecné kňazstvo všetkých veriacich od sviatostného kňazstva?
                </h3>

                <p>
                    Každý kresťan je povolaný k&nbsp;všeobecnému kňazstvu, aby v&nbsp;Božom mene pôsobil vo svete a&nbsp;prinášal mu Božie požehnanie a&nbsp;milosť. Pri Poslednej večeri a&nbsp;pri rozoslaní apoštolov dal však Ježiš niektorým posvätnú moc na službu veriacim. Preto len títo vysvätení kňazi zastupujú Ježiša, Pastiera svojho ľudu a&nbsp;Hlavu svojho tela - Cirkvi. Všetci by sme mali nanovo objaviť Boží dar pre Cirkev, ktorým je vysvätený kňaz, ktorý medzi nami zastupuje samého Pána.
                </p>
