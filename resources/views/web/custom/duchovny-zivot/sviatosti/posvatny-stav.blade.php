        <x-web.page.subsection title="Čo je sviatosť posvätného stavu?">
            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="sacr6-004" side="right" dimensionSource="medium" columns="4" />

                <p>
                    Ten, kto prijíma sviatosť posvätného stavu, dostáva osobitnú účasť na Kristovom kňazstve, ktoré sa kvalitatívne líši od všeobecného kňazstva veriacich. Sviatostné kňazstvo je dar Ducha Svätého, ktorý udeľuje Kristus prostredníctvom Cirkvi. Zo sviatostného kňazstva nevyplýva úloha plniť iba určitú funkciu alebo zastávať nejaký úrad. Vysviackou dostáva kňaz konkrétnu moc a&nbsp;poslanie slúžiť svojim bratom a&nbsp;sestrám vo viere. Sviatosť posväteného stavu má tri stupne: biskupský stav (episkopát), kňazský stav (presbyterát), diakonský stav (diakonát).
                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">
                    Posvätný stav v&nbsp;Božom pláne spásy
                </h3>

                <p>
                    Kňazi Starého zákona boli predovšetkým prostredníkmi medzi nebo a&nbsp;zemou, medzi Bohom a&nbsp;jeho národom. Ježiš Kristus ako jediný „prostredník medzi Bohom a&nbsp;ľuďmi“ (1Tim 2,5) toto starozákonné kňazstvo naplnil, dovŕšil a&nbsp;ukončil. Po Ježišovej vykupiteľskej obete môže existovať už iba sviatostné kňazstvo v&nbsp;Kristovi, v&nbsp;Kristovej obete na kríži a&nbsp;skrze Kristovo povolanie a&nbsp;apoštolské poslanie. Katolícky kňaz, ktorý udeľuje sviatosti, nekoná na vlastnú päsť, z&nbsp;vlastnej moci alebo zo svojej morálnej dokonalosti, ale „in persona Christi“ - v&nbsp;osobe Krista. Vysviackou v&nbsp;ňom rastie Kristova premieňajúca, uzdravujúca a&nbsp;zachraňujúca moc, ktorou slúži zverenému ľudu.
                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">
                    Kto môže prijať túto sviatosť?
                </h3>

                <p>
                    Za diakona, kňaza a&nbsp;biskupa môže byť platne vysvätený muž, ktorý prijal katolícku vieru, bol v&nbsp;nej pokrstený a&nbsp;pobirmovaný a&nbsp;je Cirkvou povolaný na tento úrad.
                </p>
            </x-web.page.text-segment>
        </x-web.page.subsection>

        <x-web.page.subsection title="Účinok diakonskej vysviacky">
            <x-web.page.text-segment animation="fromleft">

                <x-partials.picture titleSlug="sacr6-009" side="left" dimensionSource="medium" columns="4" />

                <p>
                    Diakon je ustanovený pre osobitnú službu Cirkvi. Zastupuje totiž Krista, ktorý „neprišiel dať sa obsluhovať, ale slúžiť a&nbsp;položiť svoj život ako výkupné za mnohých“ (Mt 20,28). V&nbsp;liturgii diakonskej vysviacky sa hovorí: „Posilnený darom Ducha Svätého bude pomáhať biskupovi a&nbsp;jeho kňazom v&nbsp;službe slova, oltára a&nbsp;charity. Stane sa služobníkom všetkých.“ Prvotným vzorom diakona je svätý mučeník Štefan, ktorý bol medzi prvými siedmimi diakonmi, ktorých ustanovili apoštoli, keď sa v&nbsp;službe prvotnej cirkvi v&nbsp;Jeruzaleme cítili preťažení množstvom charitatívnych úloh. Neskôr sa diakonská služba stala na dlhé storočia len nižším stupňom na ceste ku kňazstvu. Dnes sú opäť do tejto služby povolaní jednak tí, ktorí žijú v&nbsp;celibáte, ako aj ženatí muži. Takto chápaná diakonská služba nanovo zdôrazňuje služobný charakter Cirkvi. Zároveň poskytuje kňazom pomoc v&nbsp;duchu prvotnej Cirkvi tým, že diakon prijíma predovšetkým pastoračné a&nbsp;sociálne úlohy v&nbsp;Cirkvi. Aj diakonská vysviacka vtláča celoživotnú a&nbsp;nezrušiteľnú pečať.
                </p>
            </x-web.page.text-segment>
        </x-web.page.subsection>

