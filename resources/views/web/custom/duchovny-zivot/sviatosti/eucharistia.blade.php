        <x-web.page.subsection title="Spôsob prítomnosti Ježiša v&nbsp;Eucharistii">
            <x-web.page.text-segment animation="fromright">

                <p>
                    Ježiš je vo sviatosti Eucharistie prítomný tajomným, ale skutočným spôsobom. Vždy, keď Cirkev uskutočňuje Ježišovo poverenie „Toto robte na moju pamiatku.“, láme chlieb a&nbsp;podáva kalich, práve v&nbsp;tej chvíli sa uskutočňuje jedinečná a&nbsp;neopakovateľná Ježišova obeta na kríži sprítomnená na oltári; napĺňa sa tak dielo nášho vykúpenia a&nbsp;my na ňom máme skutočný podiel.
                </p>

                <x-partials.picture titleSlug="sacr3-014" side="right" dimensionSource="medium" columns="4" />

