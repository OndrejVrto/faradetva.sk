                </p>

                <h3 class="mt-2 wow fromright" data-wow-delay="0.4s">
                    Príprava pred svätým prijímaním
                </h3>

                <p>
                    Kto chce prijať sväté prijímanie, musí byť katolík. Ak si je vedomý ťažkého hriechu, musí sa predtým vyspovedať. Skôr ako pristúpi k&nbsp;oltáru, mal by sa zmieriť s&nbsp;blížnymi. Ešte pred niekoľkými rokmi bola bežná prax, že sa pred svätou omšou minimálne tri hodiny nič nejedlo; ľudia sa tak chceli pripraviť na stretnutie s&nbsp;Pánom Ježišom vo svätom prijímaní. Dnes Cirkev predpisuje zachovať hodinu pôstu pred svätým prijímaním. Iným znakom úcty je aj primerane pekné oblečenie.
                </p>

