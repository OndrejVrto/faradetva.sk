        <x-web.page.subsection title="Čo je Eucharistia">
            <x-web.page.text-segment animation="fromright">

                <x-partials.picture titleSlug="sacr3-010" side="right" dimensionSource="medium" columns="3" />

                <p>
                    Svätá Eucharistia je sviatosť, v&nbsp;ktorej Ježiš Kristus za nás dáva svoje telo a&nbsp;svoju krv - seba samého, aby sme sa mu aj my odovzdali v&nbsp;láske a&nbsp;aby sme sa s&nbsp;ním zjednotili vo svätom prijímaní. Tak sa spájame s&nbsp;jediným Kristovým telom - Cirkvou. Eucharistia je po krste a&nbsp;po birmovaní tretia iniciačná sviatosť Katolíckej cirkvi. Eucharistia je tajomným stredobodom všetkých týchto sviatostí, pretože Ježišova historická obeta na kríži sa pri premenení sprítomňuje skrytým nekrvavým spôsobom. Tak sa slávenie Eucharistie stáva „prameňom a&nbsp;vrcholom celého kresťanského života“. Eucharistia je súhrnom našej viery. Nejestvuje nič väčšie, čo by sme ešte mohli dosiahnuť. Keď prijímame eucharistický chlieb zjednocujeme sa s&nbsp;láskou Ježiša, ktorý dal za nás svoje telo na dreve kríža. Keď pijeme z&nbsp;kalicha premenené víno, zjednocujeme sa s&nbsp;tým, ktorý za nás vylial svoju krv. Tento obrad sme si nevymysleli. Sám Ježiš slávil so svojimi učeníkmi Poslednú večeru a&nbsp;predvídal v&nbsp;nej svoju smrť. Daroval sa svojim učeníkom v&nbsp;podobe chleba a&nbsp;vína a&nbsp;vyzval ich, aby od toho okamihu vždy a&nbsp;všade slávili v&nbsp;Eucharistii jeho smrť: „Toto robte na moju pamiatku.“ (1Kor 11,24)
                </p>

                <h3 class="mt-5 wow fromright" data-wow-delay="0.4s">
                    Nakoľko je Eucharistia dôležitá pre Cirkev
                </h3>

                <p>
                    Slávenie Eucharistie je jadrom kresťanského spoločenstva. Práve v&nbsp;nej sa Cirkev stáva Cirkvou. Cirkvou nie sme preto, že prispievame do kostolnej zbierky, že si navzájom dobre rozumieme alebo že nás osud zavial do určitého spoločenstva, ale preto, že v&nbsp;Eucharistii prijímame Kristovo telo a&nbsp;stávame sa jeho tajomným telom.
                </p>
            </x-web.page.text-segment>
        </x-web.page.subsection>

