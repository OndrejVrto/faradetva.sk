<?php

namespace App\Traits;

use Illuminate\Support\Str;

trait SlugFromTitle
{

    protected static function boot()
    {
        parent::boot();

        static::creating(function ($model) {
            $model->slug = Str::slug($model->title);
        });

        static::updating(function ($model) {
            $model->slug = Str::slug($model->title);
        });
    }


}
